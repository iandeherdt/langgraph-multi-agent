# ────────────────────────── trace logger ──────────────────────────


class TraceLogger:
    """JSONL trace logger. One event per line, flushed immediately. One file per task run."""

    def __init__(self, base_dir: Path):
        self.base_dir = base_dir
        self.fh = None
        self.path: Path | None = None
        self.iter = 0
        self.step = 0

    def start_task(self, task_text: str) -> Path:
        if self.fh:
            self.end_task(reason="abandoned_for_new_task")
        self.base_dir.mkdir(parents=True, exist_ok=True)
        ts = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
        slug = re.sub(r"[^a-z0-9]+", "-", task_text.lower())[:40].strip("-") or "task"
        self.path = self.base_dir / f"{ts}-{slug}.jsonl"
        self.fh = open(self.path, "w")
        self.iter = 0
        self.step = 0
        self.log("task_start", task=task_text)
        return self.path

    def end_task(self, **fields) -> None:
        if self.fh:
            try:
                self.log("task_end", **fields)
                self.fh.close()
            except Exception:
                pass
        self.fh = None
        self.path = None

    def set_iter(self, n: int) -> None:
        self.iter = n

    def set_step(self, n: int) -> None:
        self.step = n

    def log(self, kind: str, **fields) -> None:
        if not self.fh:
            return
        event = {
            "ts": datetime.now(timezone.utc).isoformat(),
            "iter": self.iter,
            "step": self.step,
            "kind": kind,
            **fields,
        }
        try:
            self.fh.write(json.dumps(event, default=str) + "\n")
            self.fh.flush()
        except Exception:
            pass  # never let logging crash the agent


TRACE = TraceLogger(TRACE_DIR)


# ────────────────────────── git checkpoints ──────────────────────────


# Per-run state for git checkpointing. Populated by _init_workspace_git_repo() at startup
# (or stays empty if checkpointing is disabled / git unavailable). Read by
# _create_checkpoint() per iteration and by main()'s end-of-run review-hint print.
_git_checkpoint_state: dict = {
    "enabled": False,        # True only when init succeeded; flips False on first failure
    "branch": None,          # branch name we created at startup
    "commit_count": 0,       # iterations that produced a commit (incl. empty markers)
    "init_commit": None,     # initial-state commit hash, for diff-against-start
    "workspace": None,       # absolute path of the git repo (defaults to WORKSPACE)
}


def _git(args: list[str], *, cwd: Path, timeout: int = 30) -> subprocess.CompletedProcess:
    """Run a git command with timeout + captured output. Doesn't raise on non-zero."""
    return subprocess.run(
        ["git", "-C", str(cwd)] + args,
        capture_output=True, text=True, timeout=timeout,
        env={**os.environ,
             # Quiet the "please tell me who you are" prompt on the initial commit.
             "GIT_AUTHOR_NAME": os.environ.get("GIT_AUTHOR_NAME", "harness"),
             "GIT_AUTHOR_EMAIL": os.environ.get("GIT_AUTHOR_EMAIL", "harness@local"),
             "GIT_COMMITTER_NAME": os.environ.get("GIT_COMMITTER_NAME", "harness"),
             "GIT_COMMITTER_EMAIL": os.environ.get("GIT_COMMITTER_EMAIL", "harness@local")},
    )


def _init_workspace_git_repo() -> None:
    """Set up the workspace git repo + per-run branch. Called once at run start.

    Idempotent for the repo (checks for .git first); always creates a fresh per-run branch
    so each invocation gets its own commit history isolated from prior runs. Fails soft:
    on any error (git not installed, permission, broken state) flips _git_checkpoint_state
    enabled=False and logs to trace; the harness keeps running uncheckpointed.
    """
    if not ENABLE_GIT_CHECKPOINTS:
        TRACE.log("git_checkpoint_disabled", reason="env_or_flag")
        return

    workspace = WORKSPACE
    _git_checkpoint_state["workspace"] = str(workspace)

    try:
        # 1. Init repo if missing.
        repo_check = _git(["rev-parse", "HEAD"], cwd=workspace)
        if repo_check.returncode != 0:
            init_result = _git(["init", "-q"], cwd=workspace)
            if init_result.returncode != 0:
                raise RuntimeError(f"git init failed: {init_result.stderr.strip()}")
            # Workspace .gitignore for harness internals (project-specific .gitignores
            # nested under <project>/ are merged automatically by git).
            gitignore_path = workspace / ".gitignore"
            if not gitignore_path.exists():
                gitignore_path.write_text(GIT_WORKSPACE_GITIGNORE)
            _git(["add", "-A"], cwd=workspace)
            initial = _git(["commit", "--allow-empty", "-m",
                            "[harness] initial state at run start"], cwd=workspace)
            if initial.returncode != 0:
                raise RuntimeError(f"initial commit failed: {initial.stderr.strip()}")

        # 2. Capture HEAD as the run start point.
        head = _git(["rev-parse", "--short", "HEAD"], cwd=workspace)
        if head.returncode != 0:
            raise RuntimeError(f"rev-parse HEAD failed: {head.stderr.strip()}")
        _git_checkpoint_state["init_commit"] = head.stdout.strip()

        # 3. Create + checkout a per-run branch (timestamped, sortable).
        ts = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
        branch_name = f"{GIT_CHECKPOINT_BRANCH_PREFIX}{ts}"
        co = _git(["checkout", "-b", branch_name], cwd=workspace)
        if co.returncode != 0:
            raise RuntimeError(f"checkout -b {branch_name} failed: {co.stderr.strip()}")
        _git_checkpoint_state["branch"] = branch_name
        _git_checkpoint_state["enabled"] = True

        TRACE.log("git_checkpoint_init", branch=branch_name,
                  init_commit=_git_checkpoint_state["init_commit"],
                  workspace=str(workspace))
        print(f"  Git checkpoints: branch {branch_name} at {_git_checkpoint_state['init_commit']}")
    except Exception as e:
        # Fail soft. Log once at init; per-iter calls won't re-attempt.
        _git_checkpoint_state["enabled"] = False
        err = str(e)
        TRACE.log("git_checkpoint_init_failed", error=err[:500])
        print(f"  WARN: git checkpointing disabled ({type(e).__name__}: {err[:200]})")


def _create_checkpoint(*, iteration: int, task: str, plan_doc: dict,
                       verdict: str, builder_model: str) -> None:
    """Commit the workspace state after an iteration. Empty if nothing changed; the marker
    is still useful for `git log --oneline` review."""
    if not _git_checkpoint_state.get("enabled"):
        return
    workspace = Path(_git_checkpoint_state["workspace"])
    try:
        # Compose commit message.
        tasks = (plan_doc or {}).get("tasks") or []
        done = sum(1 for t in tasks if t.get("status") == "done")
        total = len(tasks)
        title = (task or "(no task)").strip().splitlines()[0]
        if len(title) > 100:
            title = title[:97] + "..."
        msg = (
            f"[harness] iter {iteration}: {title}\n\n"
            f"Plan progress: {done} of {total} tasks done\n"
            f"Builder model: {builder_model}\n"
            f"Verdict: {verdict}"
        )

        # Stage everything; --allow-empty so we still get a marker for no-op iterations.
        add = _git(["add", "-A"], cwd=workspace)
        if add.returncode != 0:
            raise RuntimeError(f"git add failed: {add.stderr.strip()}")

        # Count changed files relative to the previous commit (informational).
        diff = _git(["diff", "--cached", "--name-only"], cwd=workspace)
        files_changed = [ln for ln in diff.stdout.splitlines() if ln] if diff.returncode == 0 else []

        commit = _git(["commit", "--allow-empty", "-m", msg], cwd=workspace)
        if commit.returncode != 0:
            raise RuntimeError(f"git commit failed: {commit.stderr.strip()}")

        head = _git(["rev-parse", "--short", "HEAD"], cwd=workspace)
        commit_hash = head.stdout.strip() if head.returncode == 0 else "(unknown)"
        _git_checkpoint_state["commit_count"] += 1
        # Track the latest commit so RUN_SUMMARY.md's "Last successful checkpoint" line
        # has something to render.
        _git_checkpoint_state["last_commit"] = {"iter": iteration, "hash": commit_hash}

        TRACE.log("checkpoint_created",
                  iter=iteration, commit_hash=commit_hash,
                  commit_message=msg.splitlines()[0],
                  files_changed=files_changed[:50],
                  files_changed_count=len(files_changed))
        print(f"  Checkpoint: {commit_hash} ({len(files_changed)} file(s) changed) on "
              f"{_git_checkpoint_state['branch']}")
    except Exception as e:
        # Don't crash the run on a checkpoint failure. Disable for the rest of the run so
        # we don't spam errors per iteration.
        TRACE.log("checkpoint_failed", iter=iteration, error=str(e)[:500])
        print(f"  WARN: checkpoint failed ({type(e).__name__}: {str(e)[:200]}) — disabling for rest of run")
        _git_checkpoint_state["enabled"] = False


def _emit_checkpoint_summary() -> None:
    """End-of-run hint. Always prints, even when checkpointing was disabled, so the user
    knows the state of things."""
    state = _git_checkpoint_state
    if not state.get("branch"):
        return  # never initialized
    branch = state["branch"]
    workspace = state["workspace"]
    n = state["commit_count"]
    TRACE.log("run_branch_created", branch=branch, commit_count=n,
              workspace=workspace, init_commit=state.get("init_commit"))
    print()
    print(f"━━━ Run checkpoints: {n} commit(s) on {branch} ━━━")
    print(f"  Review this run: git -C {workspace} log {branch} --oneline")
    if n > 0:
        print(f"  Diff against run start: git -C {workspace} diff "
              f"{state.get('init_commit', 'HEAD~' + str(n))} {branch}")


