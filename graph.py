# Evaluator caches (built once per process):
# - agent: the create_agent return value
# - mcp_session_cm: the async context manager that owns the persistent MCP SSE connection
# - mcp_session: the ClientSession yielded by mcp_session_cm
# A *persistent* MCP session is mandatory for the evaluator to do useful browser-based
# verification. The default langchain-mcp-adapters mode (`client.get_tools()`) opens a
# fresh session per tool call — and Playwright MCP gives each session a fresh page, so
# every call after browser_navigate sees Page URL: about:blank. Page state is destroyed
# between calls. Reproduced cleanly with a manual nav→snapshot probe; fixed by opening
# `async with client.session(...)` once and binding tools via load_mcp_tools(session).
_evaluator_holder: dict = {
    "agent": None, "mcp_session_cm": None, "mcp_session": None,
    # Track which model the cached agent was built for so the tier selector can
    # detect a model change and trigger a rebuild (different LLM → different agent).
    # The MCP session itself is reusable across model changes — we only swap the agent.
    "agent_model_id": None,
    # Most recent tier selector decision, captured for RUN_SUMMARY rendering.
    "last_selection": None,  # {model, tier_used, tier_requested, reason} or None
}


def _format_evaluator_model_line() -> str:
    """Render the RUN_SUMMARY 'Evaluator model' (and cost-limit) Status lines. Returns
    empty string when no eval has run yet."""
    sel = _evaluator_holder.get("last_selection")
    if not sel:
        return ""
    model_line = (f"- Evaluator model: {sel.get('model','?')} "
                  f"(tier={sel.get('tier_used','?')}, reason={sel.get('reason','?')!r})\n")
    ev = _evaluator_cost_limit_event
    if ev.get("iteration") is not None:
        model_line += (
            f"- Evaluator cost limit: hit at iter {ev['iteration']} "
            f"(€{ev['cost_eur']:.2f} of €{ev['limit_eur']:.2f}, "
            f"{ev['tool_calls_made']} tool call(s); last tool: {ev.get('last_tool')!r})\n"
        )
    return model_line

# In-memory record of recent eval tool calls + their (truncated) bodies. The recursion-limit
# handler scans this for findings (console errors, runtime-error dialogs, broken navigation,
# non-2xx HTTP) so a budget-overrun run still produces actionable verdict notes instead of
# generic boilerplate. Cleared at the top of each evaluator_node invocation.
_eval_tool_history: list[dict] = []

# Per-iteration file-touch tracking. Populated by str_replace + create_file as they run;
# reset at the top of each builder_node call; read by evaluator_node to decide whether
# browser verification can be skipped on a non-UI-change iteration. Module-level (vs.
# threaded through @tool args) because @tool functions don't have direct access to State,
# matching the existing _plan_holder / _exit_holder pattern.
_files_touched_holder: set[str] = set()


def _record_file_touch(path: str) -> None:
    """Record a file mutation. Normalize to absolute path string for stable matching."""
    if not path:
        return
    try:
        # Resolve to absolute even if the path was relative; the matcher then doesn't have
        # to care about whether the builder used /workspace/proj/x or proj/x or x.
        p = str(Path(path).resolve()) if not Path(path).is_absolute() else str(path)
    except Exception:
        p = str(path)
    _files_touched_holder.add(p)


def _iteration_affects_ui(touched_files) -> bool:
    """True if any touched file matches a UI-relevant pattern.

    Patterns are case-insensitive. Globs (containing * or ?) match by fnmatch against the
    basename; non-glob patterns match by substring against the full path. A defensive empty-
    or-None handling covers the no-touches case (always False — nothing changed).
    """
    if not touched_files:
        return False
    import fnmatch
    paths_lower = [str(p).lower() for p in touched_files]
    for pat in UI_PATH_PATTERNS:
        pat_lower = pat.lower()
        is_glob = "*" in pat or "?" in pat
        for p in paths_lower:
            if is_glob:
                # fnmatch against basename AND full path — the glob *.css should hit both
                # /workspace/proj/styles.css and a hypothetical app/styles.css.
                base = p.rsplit("/", 1)[-1]
                if fnmatch.fnmatch(base, pat_lower) or fnmatch.fnmatch(p, pat_lower):
                    return True
            else:
                if pat_lower in p:
                    return True
    return False


async def build_evaluator_subagent(llm=None):
    """Build (or rebuild) the evaluator agent. The optional `llm` parameter lets the
    tier selector pass a per-call-chosen model; default falls back to the legacy
    cheap-tier `evaluator_llm` global.

    Reuses the existing MCP session if one is already open (only the agent rebuilds
    when the model changes; the SSE session is reusable across model swaps). Builds
    a new session only if none exists or the prior one was closed."""
    mcp_url = os.environ.get("PLAYWRIGHT_MCP_URL", "http://playwright-mcp:8931/sse")
    chosen_llm = llm or evaluator_llm

    # Persistent MCP session: open ONE session for the lifetime of the evaluator subagent.
    # langchain-mcp-adapters has two modes:
    #   1. client.get_tools() — returns tools that open a FRESH session per call. Default,
    #      and broken for browser-based verification: each call gets a fresh Playwright
    #      page, so any state (current URL, cookies, console messages) is lost between
    #      calls. browser_navigate succeeds, then browser_snapshot returns about:blank.
    #   2. async with client.session(name) → load_mcp_tools(session) — tools are bound to
    #      a single persistent session, so all calls share browser state. We use this.
    # Reuse: if the holder already has a live session, reuse it (only the agent needs to
    # be rebuilt when the model changes). Otherwise open a new one.
    mcp_tools: list = []
    if _evaluator_holder.get("mcp_session") is not None:
        try:
            mcp_tools = await load_mcp_tools(_evaluator_holder["mcp_session"])
        except Exception as e:
            print(f"  WARN: failed to reuse MCP session, opening new: {type(e).__name__}: {e}")
            _evaluator_holder["mcp_session"] = None

    if not mcp_tools:
        client = MultiServerMCPClient({
            "playwright": {"url": mcp_url, "transport": "sse"},
        })
        try:
            session_cm = client.session("playwright")
            session = await session_cm.__aenter__()
            _evaluator_holder["mcp_session_cm"] = session_cm
            _evaluator_holder["mcp_session"] = session
            mcp_tools = await load_mcp_tools(session)
            print(f"  Loaded {len(mcp_tools)} Playwright MCP tools (persistent session)")
        except Exception as e:
            print(f"  WARN: failed to connect to Playwright MCP at {mcp_url}: {type(e).__name__}: {e}")
            for sub in getattr(e, "exceptions", ()):
                print(f"    cause: {type(sub).__name__}: {sub}")
            print("  WARN: evaluator will run with code-only tools (no screenshots).")

    # Make individual tool failures recoverable. By default, BaseTool.handle_tool_error
    # is False and a ToolException raised by a tool propagates through .ainvoke / .astream
    # and out of the subagent, terminating the entire eval. With True, the exception's str()
    # is returned to the agent as the tool result instead, so the model sees the error
    # message and can retry with different args (e.g., screenshot with a valid filename).
    for t in mcp_tools:
        t.handle_tool_error = True

    return create_agent(
        chosen_llm,
        tools=[view_file, list_dir, run_shell_oneshot, serve_in_background, stop_servers] + mcp_tools,
        **{_AGENT_PROMPT_KWARG: EVALUATOR_SYSTEM_PROMPT},
    )


async def _close_evaluator_mcp_session() -> None:
    """Gracefully close the persistent MCP SSE session at process shutdown. Idempotent —
    safe to call when no session was ever opened (e.g., MCP wasn't reachable at startup)."""
    cm = _evaluator_holder.get("mcp_session_cm")
    if cm is None:
        return
    try:
        await cm.__aexit__(None, None, None)
    except Exception as e:
        # Don't let cleanup errors mask the real reason the process is shutting down.
        print(f"  (warn: error closing MCP session: {type(e).__name__}: {str(e)[:200]})")
    finally:
        _evaluator_holder["mcp_session_cm"] = None
        _evaluator_holder["mcp_session"] = None


async def _reconnect_evaluator_mcp_session() -> None:
    """Tear down the dead MCP session + agent and rebuild from scratch.

    Call when the persistent SSE session has died mid-eval (peer disconnect, network blip,
    MCP-side resource exhaustion). Best-effort cleanup of the dead session; build_evaluator_
    subagent does the rebuild and re-binds tools to a fresh session. The OLD agent is dropped
    because its tools are bound to the dead session.

    Raises whatever build_evaluator_subagent raises if the reconnect itself fails — the
    caller (recovery loop) catches and retries with backoff."""
    # Best-effort close of the dead session. The session_cm's __aexit__ may itself raise
    # ClosedResourceError or similar — that's expected; the session is already dead. We
    # just want to stop tracking it.
    cm = _evaluator_holder.get("mcp_session_cm")
    if cm is not None:
        try:
            await cm.__aexit__(None, None, None)
        except Exception:
            pass
    _evaluator_holder["mcp_session_cm"] = None
    _evaluator_holder["mcp_session"] = None
    # Drop the old agent — its tools are bound to the dead session and would fail on
    # the next call. build_evaluator_subagent constructs a new agent with new tools.
    _evaluator_holder["agent"] = None
    _evaluator_holder["agent"] = await build_evaluator_subagent()


def _is_mcp_transport_error(err_str: str) -> bool:
    """True if the exception message matches a known transport-failure pattern. Used by
    the recovery handler to decide whether to attempt reconnect or fall through to the
    incomplete-verdict path immediately."""
    if not err_str:
        return False
    lc = err_str.lower()
    return any(p.lower() in lc for p in MCP_TRANSPORT_ERROR_PATTERNS)


def _tool_msg_content_str(content) -> str:
    """Normalize a langchain ToolMessage.content for display + logging.

    LangChain's ToolMessage.content is either a string (legacy / simple text tools) or a
    list of content blocks (multimodal — what MCP tools like browser_navigate /
    browser_take_screenshot return on @playwright/mcp@0.0.73). The list contains dicts with
    a "type" field ("text", "image", "image_url"). Calling .strip() on the list crashed the
    evaluator with `AttributeError: 'list' object has no attribute 'strip'` and routed every
    eval to verdict=continue, which produced builder→eval→crash→builder loops.

    For text blocks: extract the text. For image blocks: emit "[image]" so we know one was
    present without dumping base64 / URLs into the trace. Anything else: stringified, capped.
    """
    if content is None:
        return ""
    if isinstance(content, str):
        return content
    if isinstance(content, list):
        parts: list[str] = []
        for item in content:
            if isinstance(item, str):
                parts.append(item)
            elif isinstance(item, dict):
                t = item.get("type")
                if t == "text":
                    parts.append(str(item.get("text", "")))
                elif t in ("image", "image_url"):
                    parts.append("[image]")
                else:
                    parts.append(str(item)[:200])
            else:
                parts.append(str(item)[:200])
        return "\n".join(parts)
    return str(content)


async def _stream_subagent(subagent, prompt: str, label: str, recursion_limit: int,
                           tool_counter: dict | None = None) -> str:
    """Stream a subagent's tool calls/results to stdout + trace.

    `tool_counter` (optional): dict that gets `tool_counter[tool_name] += 1` per tool call.
    Used by evaluator_node to enforce browser-tool minimums on web-app tasks (Layer 2 evidence
    enforcement). Caller passes a fresh dict each invocation; we never reset it.
    """
    final_text = ""
    # Per-label idle thresholds: eval composes verdict prose between tool calls and may
    # pause 30-60s mid-synthesis. Builder/planner should be moving fast; tighter threshold
    # surfaces real stalls. Single global default would either spam idle warnings during
    # legitimate eval composition OR miss real builder hangs.
    if label == "eval":
        threshold_s = EVAL_HEARTBEAT_THRESHOLD_SECONDS
        interval_s = EVAL_HEARTBEAT_INTERVAL_SECONDS
    else:
        threshold_s = HEARTBEAT_THRESHOLD_SECONDS
        interval_s = HEARTBEAT_INTERVAL_SECONDS
    stream = subagent.astream(
        {"messages": [HumanMessage(content=prompt)]},
        config={"recursion_limit": recursion_limit},
        stream_mode="updates",
    ).__aiter__()
    last_event_time = time.monotonic()
    idle_ticks = 0
    last_eval_tool: str | None = None
    eval_tool_call_count = 0
    while True:
        # Cost-cap enforcement on the evaluator. Checked between events (after each node
        # update — including any new tool call surfaced — has been processed). Raises
        # EvaluatorBudgetExhausted; the caller catches and synthesizes a verdict from
        # whatever evidence has been captured in _eval_tool_history so far.
        if (label == "eval"
                and EVALUATOR_COST_LIMITS_ENABLED
                and _current_evaluator_cost_eur >= EVALUATOR_COST_LIMIT_EUR):
            TRACE.log("evaluator_cost_limit_exceeded",
                      cost_eur=round(_current_evaluator_cost_eur, 4),
                      limit_eur=EVALUATOR_COST_LIMIT_EUR,
                      tool_calls_made=eval_tool_call_count,
                      last_tool=last_eval_tool)
            print(f"\n  EVALUATOR COST LIMIT: €{_current_evaluator_cost_eur:.4f} >= "
                  f"€{EVALUATOR_COST_LIMIT_EUR:.4f}  (last tool: {last_eval_tool!r}) — "
                  f"terminating eval and synthesizing verdict from partial evidence.")
            try:
                await stream.aclose()
            except Exception:
                pass
            raise EvaluatorBudgetExhausted(
                cost_eur=_current_evaluator_cost_eur,
                limit_eur=EVALUATOR_COST_LIMIT_EUR,
                last_tool=last_eval_tool,
            )
        try:
            event = await asyncio.wait_for(stream.__anext__(), timeout=interval_s)
        except StopAsyncIteration:
            break
        except asyncio.TimeoutError:
            idle_for = time.monotonic() - last_event_time
            if idle_for >= threshold_s:
                idle_ticks += 1
                print(f"  ·· {label} [idle {int(idle_for)}s]", flush=True)
                TRACE.log("subagent_idle", label=label, idle_ms=int(idle_for * 1000),
                          idle_ticks=idle_ticks)
            continue
        last_event_time = time.monotonic()
        for _, node_output in event.items():
            for msg in node_output.get("messages", []):
                # Token accounting for any AIMessage that carries usage metadata. With
                # stream_mode="updates" each AIMessage appears once in a node update, so
                # this doesn't double-count. Tool messages don't have usage; _extract_usage
                # returns (0, 0, "") and _record_cost no-ops. Best-effort throughout.
                try:
                    in_t, out_t, model_from_msg = _extract_usage(msg)
                    if in_t or out_t:
                        _record_cost(model_from_msg or label, in_t, out_t, label=label)
                except Exception as _cost_exc:
                    TRACE.log("model_call_cost_error", label=label, error=str(_cost_exc)[:200])
                if getattr(msg, "tool_calls", None):
                    for tc in msg.tool_calls:
                        print(f"  [{label}] {tc['name']}({_format_args(tc.get('args', {}))})")
                        TRACE.log("eval_tool_call", tool=tc["name"], args=tc.get("args", {}))
                        if tool_counter is not None:
                            tool_counter[tc["name"]] = tool_counter.get(tc["name"], 0) + 1
                        if label == "eval":
                            last_eval_tool = tc["name"]
                            eval_tool_call_count += 1
                            _eval_tool_history.append({
                                "kind": "call", "tool": tc["name"], "args": tc.get("args", {}),
                            })
                            if len(_eval_tool_history) > EVAL_TOOL_HISTORY_FOR_FINDINGS * 2:
                                _eval_tool_history.pop(0)
                elif getattr(msg, "type", None) == "tool":
                    body_str = _tool_msg_content_str(msg.content)
                    body = body_str.strip().replace("\n", "\\n")
                    print(f"  [{label}-result] {msg.name} -> {_truncate_simple(body)}")
                    TRACE.log("eval_tool_result", tool=msg.name, output_chars=len(body_str))
                    if label == "eval":
                        _eval_tool_history.append({
                            "kind": "result", "tool": msg.name, "body": body_str[:4000],
                        })
                        if len(_eval_tool_history) > EVAL_TOOL_HISTORY_FOR_FINDINGS * 2:
                            _eval_tool_history.pop(0)
                elif msg.content:
                    final_text = _tool_msg_content_str(msg.content)
    return final_text


_WEB_APP_BUILDER_SIGNALS = (
    "next build", "next dev", "npm run build", "npm run dev", "pnpm build",
    "pnpm dev", "yarn build", "yarn dev", "vite build", "vite dev", "vite preview",
    "serve_in_background", "localhost:3000", "0.0.0.0:3000",
)
_WEB_APP_STACK_KEYWORDS = (
    "next.js", "nextjs", "next ", "react", "vue", "svelte", "remix", "astro",
    "express", "fastify", "hono", "nuxt", "gatsby",
)


def _is_web_app_task(state: State, plan_doc: dict) -> bool:
    """Decide whether this task delivers a web app that must be browser-verified.

    Signals (any one is enough):
    1. Builder ran a web-app build/dev/serve command (per builder_summary text)
    2. Plan architecture.stack mentions a web framework
    3. Plan requirements/tasks mention admin / page / login / dashboard / route

    False positives are acceptable: an over-detection just forces the evaluator to attempt
    browser tools, which is harmless on non-web tasks (the tool calls fail / are unused and
    the harness eventually accepts whatever evidence the evaluator produced).
    """
    summary = (state.get("builder_summary") or "").lower()
    if any(sig in summary for sig in _WEB_APP_BUILDER_SIGNALS):
        return True
    arch = plan_doc.get("architecture") or {}
    stack_text = " ".join(str(arch.get(k, "")) for k in ("stack", "key_decisions", "summary")).lower()
    if any(kw in stack_text for kw in _WEB_APP_STACK_KEYWORDS):
        return True
    # Last-resort signal: tasks/requirements mention web-app concepts
    blob = " ".join([
        " ".join(str(r) for r in plan_doc.get("requirements", [])),
        " ".join(str(t.get("text", "")) for t in plan_doc.get("tasks", [])),
    ]).lower()
    if any(kw in blob for kw in (" admin ", "/admin", "login", "dashboard", "homepage", "page editor")):
        return True
    return False


def _extract_eval_findings(history: list[dict]) -> dict:
    """Scan recent eval tool calls + results for both negative and positive observations.

    Returns:
        findings:           list[str] — human-readable failure observations (console errors,
                            runtime error dialogs, broken redirects, non-2xx responses)
        pages_ok:           list[str] — URLs navigated cleanly with no observed errors
        pages_with_errors:  list[str] — URLs where at least one finding fired
        screenshots_taken:  int — count of browser_take_screenshot calls
        interactions:       int — count of browser_click + browser_type + browser_fill_form
                            + browser_run_code_unsafe calls (proxy for "exercised the UI")
        navigates:          int — count of browser_navigate calls

    Used by the recursion-limit handler AND the empty-notes-cap-exceeded escalation: a
    budget-overrun OR communication-failed eval still produces actionable verdict notes.
    Failure findings are surfaced for the obvious bug case (eval saw a runtime error and
    didn't write it down). Positive observations are surfaced for the harder case where the
    eval browsed cleanly but produced empty notes — without these the salvage emits "no
    extractable findings" even though it has a full trail of successful navigates +
    screenshots + form interactions. The next planner needs to know the eval DID exercise
    the app even if it didn't synthesize a verdict.
    """
    findings: list[str] = []
    pages_ok: list[str] = []
    pages_with_errors: list[str] = []
    seen_urls: set[str] = set()
    screenshots_taken = 0
    interactions = 0
    navigates = 0
    _interactive_tools = {
        "browser_click", "browser_type", "browser_fill_form",
        "browser_press_key", "browser_run_code_unsafe", "browser_select_option",
    }

    # Pair each result with the call that produced it, so we know what URL was being
    # browsed when, e.g., a snapshot showed a runtime-error dialog. last_nav_url tracks
    # the most recent browser_navigate target — it's the page state for any subsequent
    # snapshot/screenshot/console_messages until the next navigate.
    last_nav_url: str | None = None
    last_click_target: str | None = None

    for i, entry in enumerate(history):
        if entry["kind"] == "call":
            if entry["tool"] == "browser_navigate":
                last_nav_url = (entry.get("args") or {}).get("url")
                navigates += 1
            elif entry["tool"] == "browser_click":
                # The args usually contain a `ref` or `element` description we can quote.
                args = entry.get("args") or {}
                last_click_target = args.get("element") or args.get("ref") or "unknown element"
                interactions += 1
            elif entry["tool"] == "browser_take_screenshot":
                screenshots_taken += 1
            elif entry["tool"] in _interactive_tools:
                interactions += 1
            continue

        # entry["kind"] == "result"
        body = entry.get("body") or ""
        tool = entry.get("tool", "")

        # Pull the URL out of the result body (more authoritative than last_nav_url for
        # cases where the page redirected after the navigate).
        m = re.search(r"Page URL:\s*(\S+)", body)
        body_url = m.group(1) if m else None
        url_for_finding = body_url or last_nav_url or "(unknown)"
        if body_url and body_url != "about:blank":
            seen_urls.add(body_url)

        # Pattern 1: "Console: N errors, M warnings" inline in browser_navigate result.
        m = re.search(r"Console:\s*(\d+)\s*errors?,\s*(\d+)\s*warnings?", body, re.IGNORECASE)
        if m:
            errs = int(m.group(1))
            warns = int(m.group(2))
            if errs > 0:
                findings.append(f"{errs} console error(s) on {url_for_finding} (also {warns} warning(s))")
                pages_with_errors.append(url_for_finding)

        # Pattern 2: browser_console_messages explicit error count.
        m = re.search(r"Total messages:\s*\d+\s*\(Errors:\s*(\d+),\s*Warnings:\s*(\d+)\)", body)
        if m:
            errs = int(m.group(1))
            if errs > 0:
                findings.append(f"console_messages reported {errs} error(s) on {url_for_finding}")
                pages_with_errors.append(url_for_finding)

        # Pattern 3: "Unhandled Runtime Error" or Next.js error overlay in snapshot text.
        if re.search(r"Unhandled\s+Runtime\s+Error|Application\s+error.*client-side\s+exception",
                     body, re.IGNORECASE):
            findings.append(f"runtime error overlay visible on {url_for_finding}")
            pages_with_errors.append(url_for_finding)

        # Pattern 4: navigation after click went somewhere unexpected. We can't know the
        # "expected" target without semantic context, but we CAN flag clicks that landed
        # on /login when the user clicked a link labeled like a non-login destination.
        if tool == "browser_click" and last_click_target:
            if body_url and "login" in body_url.lower() and "login" not in last_click_target.lower():
                findings.append(
                    f"click on {last_click_target!r} unexpectedly landed on {body_url} "
                    f"(likely auth/middleware bug)"
                )
                pages_with_errors.append(body_url)
            last_click_target = None  # consume

        # Pattern 5: non-2xx HTTP from curl in shell output. Most common form is the curl
        # -w "%{http_code}" pattern returning just digits + maybe stderr.
        if tool == "run_shell_oneshot":
            m = re.search(r"^\s*([45]\d\d)\s*$", body, re.MULTILINE)
            if m and "curl" in (body or ""):
                # Only flag if a curl command was clearly involved — otherwise 4xx/5xx
                # in body could be unrelated text. (Imperfect; better than nothing.)
                pass
            # Look for explicit curl HTTP error in stderr
            m = re.search(r"HTTP/\S+\s+(\d\d\d)\s+\w+", body)
            if m:
                code = int(m.group(1))
                if code >= 400:
                    findings.append(f"curl observed HTTP {code} response (see body)")

    # pages_ok = URLs we saw that aren't in the errors list.
    for u in seen_urls:
        if u not in pages_with_errors and u != "about:blank":
            pages_ok.append(u)

    # Dedupe while preserving order.
    def _dedupe(xs: list) -> list:
        seen: set = set()
        out: list = []
        for x in xs:
            if x not in seen:
                seen.add(x)
                out.append(x)
        return out

    return {
        "findings": _dedupe(findings),
        "pages_ok": _dedupe(pages_ok),
        "pages_with_errors": _dedupe(pages_with_errors),
        "screenshots_taken": screenshots_taken,
        "interactions": interactions,
        "navigates": navigates,
    }


def _build_evidence_summary_for_retry(history: list[dict], *,
                                       max_calls: int = 20,
                                       per_result_chars: int = 500) -> dict:
    """From the evaluator's tool calls in this invocation, build a concise summary the
    model can use to write a verdict without re-running tools.

    Used by the empty-notes retry path. The retry's prior failure was "verdict written
    but NOTES was blank" — the model HAD evidence, just failed to surface it. The fix is
    to re-show the model what it already gathered and ask it to write findings without
    making more tool calls.

    Pairs each `call` entry with its following `result` entry (when present) so the
    summary reads: "browser_console_messages(...) → result body". Truncates each result
    to `per_result_chars` so the prompt doesn't blow up on console-message dumps.

    Returns a dict {summary, calls_total, calls_shown} so the caller can log accurate
    counts. `calls_total` is every call in `history`; `calls_shown` is what actually
    ended up in the summary after the tail-cap (≤ max_calls).
    """
    if not history:
        return {"summary": "", "calls_total": 0, "calls_shown": 0}

    lines = ["You already gathered the following evidence in this evaluator call:"]
    # Pair calls with their immediately-following results. The history is recorded in
    # order: a call event, then a result event with the same tool name.
    pairs: list[tuple[dict, dict | None]] = []
    i = 0
    while i < len(history):
        entry = history[i]
        if entry.get("kind") == "call":
            result = None
            if i + 1 < len(history) and history[i + 1].get("kind") == "result":
                result = history[i + 1]
                i += 2
            else:
                i += 1
            pairs.append((entry, result))
        else:
            i += 1

    calls_total = len(pairs)
    # Cap pairs from the tail (most-recent — the model's latest observations are usually
    # the most decision-relevant for verdict writing).
    if len(pairs) > max_calls:
        pairs = pairs[-max_calls:]
        lines.append(f"(showing last {max_calls} of {calls_total} tool calls)")

    for idx, (call, result) in enumerate(pairs, 1):
        tool = call.get("tool", "?")
        args = call.get("args") or {}
        args_str = _format_args(args) if args else ""
        # Highlight URLs / paths in args inline so the model can correlate without re-reading.
        head = f"{idx}. {tool}({args_str})"
        lines.append(head)
        if result:
            body = (result.get("body") or "").strip()
            if not body:
                lines.append("   → (empty result)")
            else:
                # Single-line preview — collapse newlines to keep the summary readable.
                body_collapsed = body.replace("\n", " ⏎ ")
                if len(body_collapsed) > per_result_chars:
                    body_collapsed = body_collapsed[:per_result_chars] + " …[truncated]"
                lines.append(f"   → {body_collapsed}")
        else:
            lines.append("   → (no result captured — call may not have completed)")

    lines.append("")
    lines.append(
        "Write your verdict NOW based on this evidence. Do NOT make additional tool "
        "calls. If the evidence above shows console errors, runtime-error overlays, "
        "non-2xx responses, or missing seeded content, your verdict should reflect "
        "that. If the evidence is genuinely insufficient to make a verification call, "
        "return verdict=incomplete with that explanation."
    )
    return {"summary": "\n".join(lines), "calls_total": calls_total, "calls_shown": len(pairs)}


def _format_findings_for_notes(findings_data: dict, *, header: str) -> str:
    """Format extracted findings as multi-line verdict notes.

    Two main shapes depending on what the extractor saw:
      - With failure findings: list them, then append pages-with-errors / pages-ok summary.
      - Without failure findings: surface positive activity (N pages browsed, M screenshots,
        K interactions) so the next planner knows the eval DID exercise the app even if it
        didn't synthesize a verdict.
    Either way: the notes are actionable evidence, not generic "no observations" boilerplate.
    """
    findings = findings_data.get("findings") or []
    pages_ok = findings_data.get("pages_ok") or []
    pages_err = findings_data.get("pages_with_errors") or []
    nav = findings_data.get("navigates", 0)
    snaps = findings_data.get("screenshots_taken", 0)
    inters = findings_data.get("interactions", 0)

    lines = [header]
    if findings:
        lines.append("Findings extracted from tool history:")
        for f in findings:
            lines.append(f"  - {f}")
        if pages_err:
            lines.append(f"Pages with observed errors: {', '.join(pages_err)}")
        if pages_ok:
            lines.append(f"Pages verified without observed errors: {', '.join(pages_ok)}")
    elif pages_ok or nav or snaps or inters:
        # No failure findings, but the eval clearly exercised the app. Report the activity
        # so the next planner doesn't think the eval did nothing.
        lines.append(
            f"No failure signals extracted, BUT the eval did exercise the app: "
            f"{nav} navigate(s), {snaps} screenshot(s), {inters} interaction(s) "
            f"(clicks / form fills / page-script runs)."
        )
        if pages_ok:
            lines.append(
                f"Pages browsed cleanly with no observed console errors / runtime overlays: "
                f"{', '.join(pages_ok)}"
            )
        lines.append(
            "The work may actually be done — the model just failed to write the findings "
            "down. Recommend: re-run with narrower EVALUATOR_INSTRUCTIONS asking the eval "
            "to verify a SPECIFIC behavior on each named URL (e.g., 'on /admin/menu, "
            "confirm sidebar does not overlap content; quote the snapshot heading text')."
        )
    else:
        # Truly nothing — neither failures observed nor browsing activity.
        lines.append(
            "No actionable observations were salvageable from the tool history. The eval "
            "may have spent its budget on shell/file inspection without exercising the "
            "running app. Recommend: planner narrows EVALUATOR_INSTRUCTIONS to specific "
            "URLs and what to look for on each."
        )
    return "\n".join(lines)


def _eval_evidence_shortfall(tool_counter: dict) -> list[str]:
    """Return a list of human-readable shortfalls vs. EVAL_MIN_* thresholds. Empty = sufficient."""
    nav = tool_counter.get("browser_navigate", 0)
    snap = tool_counter.get("browser_take_screenshot", 0)
    click = tool_counter.get("browser_click", 0)
    short = []
    if nav < EVAL_MIN_NAVIGATE_CALLS:
        short.append(f"browser_navigate calls: {nav} < required {EVAL_MIN_NAVIGATE_CALLS}")
    if snap < EVAL_MIN_SCREENSHOT_CALLS:
        short.append(f"browser_take_screenshot calls: {snap} < required {EVAL_MIN_SCREENSHOT_CALLS}")
    if click < EVAL_MIN_CLICK_CALLS:
        short.append(f"browser_click calls: {click} < required {EVAL_MIN_CLICK_CALLS}")
    return short


def _eval_notes_too_short(notes: str) -> bool:
    """True when the verdict's NOTES content is too thin to act on for a web-app task.

    Definition: under EVAL_NOTES_MIN_CHARS (currently 100) of non-whitespace content.
    Catches the empty-notes failure mode — model wrote `NOTES:` with nothing after, or
    just one short sentence — that the advisor was rejecting upstream after the eval had
    already exited. Now we catch inline and force one retry with a corrective preamble.
    """
    return len((notes or "").strip()) < EVAL_NOTES_MIN_CHARS


async def evaluator_node(state: State) -> dict:
    # Tier-aware model selection. Picks the evaluator model per-call from
    # HARNESS_EVALUATOR_MODEL / HARNESS_EVALUATOR_TIER and the auto heuristic. Most runs
    # land on the same model across iterations; the agent is cached + rebuilt only when
    # the chosen model differs from what's cached.
    chosen = _select_evaluator_model(state, state.get("task", ""))
    chosen_model = chosen["model"]
    _evaluator_holder["last_selection"] = dict(chosen)  # for RUN_SUMMARY rendering
    TRACE.log("evaluator_model_chosen",
              iteration=state["iteration"],
              tier_requested=chosen["tier_requested"],
              tier_used=chosen["tier_used"],
              model=chosen_model,
              reason=chosen["reason"])

    if (_evaluator_holder.get("agent") is None
            or _evaluator_holder.get("agent_model_id") != chosen_model):
        try:
            chosen_llm = _make_llm_for_model(chosen_model)
        except Exception as e:
            # Bad slug. Fall back to the legacy cheap-tier global. Logged so the operator
            # notices via grep on the trace.
            TRACE.log("evaluator_model_build_failed",
                      model=chosen_model, error=str(e)[:300])
            chosen_llm = evaluator_llm
            chosen_model = (getattr(evaluator_llm, "model_name", None)
                            or getattr(evaluator_llm, "model", None) or "unknown")
        _evaluator_holder["agent"] = await build_evaluator_subagent(llm=chosen_llm)
        _evaluator_holder["agent_model_id"] = chosen_model
    print(f"\n━━━ EVALUATOR (iteration {state['iteration']}) [{chosen_model} / "
          f"tier={chosen['tier_used']}] ━━━")
    print(f"  Selection: {chosen['reason']}")
    TRACE.log("evaluator_start", iteration=state["iteration"], model=chosen_model)
    # Reset per-iteration findings buffer. _stream_subagent populates it as eval tool
    # results arrive; the recursion-limit handler reads it to salvage findings into the
    # verdict notes when the eval times out without writing its own verdict block.
    _eval_tool_history.clear()
    # Reset per-evaluator cost cap counter. _record_cost accumulates into
    # _current_evaluator_cost_eur on each "eval"-labeled model call; _stream_subagent
    # checks before each event and raises EvaluatorBudgetExhausted on overrun.
    global _current_evaluator_cost_eur
    _current_evaluator_cost_eur = 0.0

    plan_doc = state.get("plan") or _empty_plan_doc()
    plan_render = _render_plan_doc(plan_doc)
    requirements_render = _render_requirements(plan_doc.get("requirements", []))
    is_web_app = _is_web_app_task(state, plan_doc)

    # Skip-eval check: if the iteration touched no UI files AND we have a cached
    # successful eval to fall back on, reuse the cached verdict instead of running the
    # full browser protocol. Browser verification is the dominant cost on long runs and
    # type-error fixes / server-side refactors / build tweaks don't change rendered UI.
    # Sonnet still runs the advisor (verify_completion); only the evaluator stage is
    # skipped — the cost saving is the 50+ MCP tool calls per protocol pass, not the
    # advisor call itself.
    touched = state.get("iteration_files_touched") or []
    last_eval = state.get("last_successful_eval") or {}
    skip_eligible = (
        EVAL_SKIP_ENABLED
        and is_web_app                           # only meaningful on web-app tasks
        and not _iteration_affects_ui(touched)
        and (not EVAL_SKIP_REQUIRES_PRIOR_EVAL or last_eval)
        and last_eval.get("verdict") in ("done", "continue")
    )
    if skip_eligible:
        cached_verdict = last_eval["verdict"]
        cached_notes = last_eval.get("notes_preview") or "(no cached notes)"
        cached_iter = last_eval.get("iteration", "?")
        skip_reason = (
            f"Eval skipped: this iteration touched {len(touched)} file(s), none of which "
            f"match UI patterns ({', '.join(UI_PATH_PATTERNS[:5])}, …). Reusing the "
            f"verdict from iteration {cached_iter} (verdict={cached_verdict!r}). The "
            f"rendered UI cannot have changed without a UI-file edit, so the previous "
            f"browser-based verification is still representative."
        )
        notes = f"[harness: skipped browser eval — no UI files touched]\n\n{cached_notes}"
        TRACE.log("eval_skipped_no_ui_change",
                  iteration=state["iteration"],
                  touched_files=touched[:50],
                  touched_files_count=len(touched),
                  cached_from_iteration=cached_iter,
                  cached_verdict=cached_verdict)
        print(f"  EVAL SKIPPED: no UI-relevant files touched; reusing iter {cached_iter} "
              f"verdict={cached_verdict!r}")
        if touched:
            print(f"    touched (non-UI): {', '.join(touched[:6])}{'…' if len(touched) > 6 else ''}")
        # Checkpoint here too — even a skipped eval is the end of an iteration.
        _create_checkpoint(
            iteration=state["iteration"],
            task=state.get("task", ""),
            plan_doc=plan_doc,
            verdict=f"{cached_verdict} (eval-skipped)",
            builder_model=builder_llm.model_name,
        )
        # Don't refresh last_successful_eval — keep the original cached one as the source
        # of truth so subsequent skips still point at the iteration that was actually
        # browser-verified.
        _finalize_iteration_summary(state=state, verdict=cached_verdict)
        return {
            "eval_verdict": cached_verdict,
            "eval_notes": skip_reason + "\n\n" + notes,
        }
    base_prompt = (
        f"REQUIREMENTS (load-bearing contract — verify EACH one is satisfied):\n"
        f"{requirements_render}\n\n"
        f"PLAN (current state, includes ARCHITECTURE and TASKS):\n{plan_render}\n\n"
        f"YOUR VERIFICATION INSTRUCTIONS (planner's specific asks for this iteration):\n"
        f"{state['evaluator_instructions']}\n\n"
        f"BUILDER SUMMARY (claim is a starting point, NOT evidence — verify by observation):\n"
        f"{state['builder_summary']}\n\n"
        f"Verify the work and emit your verdict block at the end."
    )

    # Two-track enforcement loop on web-app tasks. Both reject thin verdicts and force a
    # retry with a targeted corrective preamble; each track has its own cap.
    #   evidence_retry: verdict=done but tool counters under EVAL_MIN_* — model claimed
    #     done without exercising the running app.
    #   empty_notes_retry: any verdict with NOTES under EVAL_NOTES_MIN_CHARS — model
    #     observed bugs but didn't write findings down. The empty-notes failure mode
    #     was producing builder→advisor→eval→empty→builder loops because the advisor
    #     correctly rejected the empty NOTES upstream, but only after the harness had
    #     already accepted them.
    evidence_retry = 0
    empty_notes_retry = 0
    # mcp_recovery_attempt counts transport-recovery rounds within this iteration. Reset
    # per evaluator_node call. mcp_just_recovered is a one-shot flag that gates the post-
    # recovery preamble injection on the next prompt build.
    mcp_recovery_attempt = 0
    mcp_just_recovered = False
    last_failure: str | None = None  # "evidence" | "empty_notes" | "mcp_recovered" | None
    text = ""
    verdict = "continue"
    notes = ""
    tool_counter: dict = {}
    while True:
        prompt = base_prompt
        if mcp_just_recovered:
            prompt = (
                f"⚠️  HARNESS NOTICE: the Playwright MCP transport was disconnected and the "
                f"harness reconnected to a fresh session (recovery round "
                f"{mcp_recovery_attempt}/{MCP_RECOVERY_MAX_RETRIES}).\n\n"
                f"Browser state has been reset. The current page is likely about:blank, the "
                f"console-messages buffer is empty, and any in-progress click/type chain needs "
                f"to restart from the beginning. Re-navigate to the URL you were verifying, "
                f"continue the verification protocol from the last completed step, and re-emit "
                f"your verdict.\n\n"
                + base_prompt
            )
            mcp_just_recovered = False
            last_failure = "mcp_recovered"
        elif last_failure == "evidence":
            shortfall = _eval_evidence_shortfall(tool_counter)
            prompt = (
                f"⚠️  YOUR PRIOR VERDICT WAS REJECTED BY THE HARNESS (round {evidence_retry}/"
                f"{EVAL_INSUFFICIENT_EVIDENCE_RETRY_CAP}).\n\n"
                f"You declared verdict=done but did NOT actually exercise the running app:\n"
                f"  - " + "\n  - ".join(shortfall) + "\n\n"
                f"You MUST use Playwright MCP browser tools (browser_navigate, "
                f"browser_take_screenshot, browser_click, browser_snapshot, "
                f"browser_console_messages) to verify the running app on this round. HTTP 200 / "
                f"build-passes / port-listening signals are NECESSARY but NOT SUFFICIENT. "
                f"Take at least one screenshot, examine it (you have vision — describe what you "
                f"see), click at least one menu item AND attempt the admin login flow. Re-emit "
                f"your verdict with that evidence in NOTES.\n\n"
                + base_prompt
            )
        elif last_failure == "empty_notes":
            # Pull the evidence the model already gathered (tool calls + their results)
            # and inject it into the retry prompt. The empty-notes failure mode is "model
            # had observations but didn't transcribe them into NOTES" — without re-feeding
            # those observations, the retry would either re-run all the tools (waste) or
            # invent findings (worse). Showing the actual prior outputs back to the model
            # is the cheap, correct fix.
            evidence = _build_evidence_summary_for_retry(_eval_tool_history)
            evidence_summary = evidence["summary"]
            TRACE.log("evaluator_empty_notes_retry_with_evidence",
                      evidence_chars=len(evidence_summary),
                      tool_calls_total=evidence["calls_total"],
                      tool_calls_shown=evidence["calls_shown"])
            prompt = (
                f"⚠️  YOUR PRIOR VERDICT HAD EMPTY NOTES (round {empty_notes_retry}/"
                f"{EVAL_EMPTY_NOTES_RETRY_CAP}).\n\n"
                f"You wrote a VERDICT line but no actionable NOTES under it. The harness "
                f"requires concrete findings: which pages were navigated, what was visible on "
                f"each (quoted from your snapshots), what console errors appeared, what "
                f"happened when nav links were clicked. Verdicts without findings are useless "
                f"to the next planner pass and are rejected by the harness.\n\n"
                + (evidence_summary + "\n\n" if evidence_summary else "")
                + f"DO NOT make additional tool calls this round. Synthesize NOTES from the "
                f"evidence above. Cite specific URLs, specific error counts, specific page "
                f"titles, specific click results.\n\n"
                + base_prompt
            )
        try:
            try:
                text = await _stream_subagent(
                    _evaluator_holder["agent"], prompt, "eval", EVAL_RECURSION_LIMIT,
                    tool_counter=tool_counter,
                )
            except (GraphRecursionError, *_MODEL_RETRY_EXCEPTIONS) as primary_err:
                # GraphRecursionError doesn't fall back (a recursion timeout is the eval's
                # own internal budget; switching models won't help). Other model API errors
                # (rate limit / transient 5xx / connection drop) DO benefit from a
                # fallback if one is configured.
                if isinstance(primary_err, GraphRecursionError):
                    raise
                if not EVALUATOR_FALLBACK_MODEL:
                    raise
                fallback_slug = EVALUATOR_FALLBACK_MODEL
                TRACE.log("evaluator_model_fallback_used",
                          iteration=state["iteration"],
                          primary_model=chosen_model,
                          fallback_model=fallback_slug,
                          primary_error_type=type(primary_err).__name__,
                          primary_error=str(primary_err)[:300])
                print(f"\n  EVALUATOR PRIMARY MODEL FAILED ({type(primary_err).__name__}); "
                      f"retrying with fallback {fallback_slug!r}...")
                try:
                    fb_llm = _make_llm_for_model(fallback_slug)
                    fb_agent = await build_evaluator_subagent(llm=fb_llm)
                    # Cache: subsequent iterations within the run will reuse the fallback
                    # if the primary keeps failing. Selector logic still runs at top of
                    # each evaluator_node call; a recovered primary will rebuild back.
                    _evaluator_holder["agent"] = fb_agent
                    _evaluator_holder["agent_model_id"] = fallback_slug
                    text = await _stream_subagent(
                        fb_agent, prompt, "eval", EVAL_RECURSION_LIMIT,
                        tool_counter=tool_counter,
                    )
                except Exception as fb_err:
                    TRACE.log("evaluator_model_fallback_exhausted",
                              iteration=state["iteration"],
                              primary_model=chosen_model,
                              fallback_model=fallback_slug,
                              fallback_error_type=type(fb_err).__name__,
                              fallback_error=str(fb_err)[:300])
                    print(f"  FALLBACK ALSO FAILED ({type(fb_err).__name__}: {str(fb_err)[:200]}); "
                          f"falling through to verdict=incomplete path.")
                    raise fb_err
        except GraphRecursionError:
            # Eval ran out of internal-step budget without producing a verdict. Don't crash
            # the whole run — return 'continue' with notes salvaged from the tool history.
            # The eval may have observed real bugs (console errors, runtime-error overlays,
            # broken redirects) and we don't want to throw that away just because it didn't
            # write a verdict block before hitting the cap.
            findings_data = _extract_eval_findings(_eval_tool_history)
            TRACE.log("evaluator_recursion_limit", limit=EVAL_RECURSION_LIMIT,
                      findings_count=len(findings_data["findings"]),
                      pages_ok=findings_data["pages_ok"],
                      pages_with_errors=findings_data["pages_with_errors"],
                      navigates=findings_data.get("navigates", 0),
                      screenshots_taken=findings_data.get("screenshots_taken", 0),
                      interactions=findings_data.get("interactions", 0))
            print(f"\n  EVALUATOR RECURSION LIMIT ({EVAL_RECURSION_LIMIT}) — returning 'continue'.")
            notes = _format_findings_for_notes(
                findings_data,
                header=(
                    f"Evaluator hit recursion limit at {EVAL_RECURSION_LIMIT} steps before "
                    f"writing a verdict block."
                ),
            )
            print(f"  NOTES: {_truncate_simple(notes, 400)}")
            _finalize_iteration_summary(state=state, verdict="continue")
            return {"eval_verdict": "continue", "eval_notes": notes}
        except EvaluatorBudgetExhausted as bex:
            # Per-evaluator cost cap fired (HARNESS_EVALUATOR_COST_LIMIT_EUR). Synthesize
            # an incomplete verdict from whatever the evaluator captured before the cap
            # hit, so the next planner / operator can see what was observed and the run
            # terminates cleanly without silently burning the whole budget on one stuck
            # verification flow.
            findings_data = _extract_eval_findings(_eval_tool_history)
            tool_call_count = sum(1 for h in _eval_tool_history if h.get("kind") == "call")
            _evaluator_cost_limit_event.update({
                "iteration": state["iteration"],
                "cost_eur": round(bex.cost_eur, 4),
                "limit_eur": round(bex.limit_eur, 4),
                "tool_calls_made": tool_call_count,
                "last_tool": bex.last_tool,
            })
            print(f"\n  EVALUATOR COST CAP — synthesizing incomplete verdict from "
                  f"{tool_call_count} tool call(s).")
            evidence_lines = []
            for h in _eval_tool_history:
                if h.get("kind") != "call":
                    continue
                tname = h.get("tool", "?")
                a = h.get("args") or {}
                a_str = _format_args(a) if a else ""
                evidence_lines.append(f"  - {tname}({a_str})")
            evidence_block = "\n".join(evidence_lines) if evidence_lines else "  (no tool calls completed)"
            findings_block = _format_findings_for_notes(
                findings_data,
                header="Findings extracted from partial tool history:",
            )
            notes = (
                f"[evaluator: cost limit exceeded after €{bex.cost_eur:.4f} of verification "
                f"(limit €{bex.limit_eur:.4f}, last tool: {bex.last_tool!r})]\n\n"
                f"Evidence captured before budget exhaustion ({tool_call_count} tool call(s)):\n"
                f"{evidence_block}\n\n"
                f"{findings_block}\n\n"
                f"The verification was incomplete. The builder's work is preserved at the "
                f"most recent checkpoint. Manual verification recommended:\n"
                f"  - Review the tool calls listed above to see what was checked.\n"
                f"  - Retry with HARNESS_EVALUATOR_COST_LIMIT_EUR raised, "
                f"HARNESS_EVALUATOR_COST_LIMIT_ENABLED=0 to disable, or fix the underlying "
                f"flow causing the eval to spiral (commonly: broken login, infinite "
                f"navigate loop, or misconfigured port)."
            )
            print(f"  NOTES: {_truncate_simple(notes, 400)}")
            _finalize_iteration_summary(state=state, verdict="incomplete")
            return {"eval_verdict": "incomplete", "eval_notes": notes}
        except Exception as e:
            # Defense in depth: any other escape from the eval subagent (tool crash, MCP failure,
            # langchain internal error) shouldn't kill the whole run. Three paths, in order:
            #   1. MCP transport disconnect (ClosedResourceError, BrokenResourceError, etc.)
            #      → recovery loop: wait + reconnect + resume the eval. After
            #      MCP_RECOVERY_MAX_RETRIES failed reconnects, fall through to (2).
            #   2. MCP / Playwright / browser-launch failure (infrastructure) →
            #      verdict=incomplete; route_after_eval terminates with a diagnostic.
            #   3. Anything else → verdict=continue.
            # Grep with: jq -c 'select(.kind == "evaluator_exception")' workspace/.trace/*.jsonl
            import traceback
            tb = traceback.format_exc()
            err_str = str(e)
            err_lc = err_str.lower()
            TRACE.log("evaluator_exception", error_type=type(e).__name__,
                      error=err_str[:500], traceback=tb[-2000:])

            # Path 1: transport recovery (intercepts before the pattern-match-to-incomplete
            # path). Patterns ALSO live in EVAL_INCOMPLETE_EXCEPTION_PATTERNS so that after
            # recovery exhaustion the existing terminal flow still fires unchanged.
            #
            # Loop structure: keep attempting reconnect with exponential backoff until
            # either (a) reconnect succeeds → restart the outer eval loop with a recovery
            # preamble, or (b) MCP_RECOVERY_MAX_RETRIES exhausted → fall through to the
            # pattern-match incomplete path. The wait grows: 10s, 20s, 40s by default.
            if MCP_RECOVERY_ENABLED and _is_mcp_transport_error(err_str):
                last_reconn_err: Exception | None = None
                while mcp_recovery_attempt < MCP_RECOVERY_MAX_RETRIES:
                    wait_s = MCP_RECOVERY_WAIT_SECONDS * (2 ** mcp_recovery_attempt)
                    TRACE.log("mcp_transport_died",
                              attempt=mcp_recovery_attempt + 1,
                              retry_cap=MCP_RECOVERY_MAX_RETRIES,
                              error_type=type(e).__name__,
                              error=err_str[:500],
                              wait_seconds=wait_s,
                              previous_reconnect_error=(str(last_reconn_err)[:200]
                                                        if last_reconn_err else None))
                    print(f"\n  MCP TRANSPORT DIED ({type(e).__name__}); waiting {wait_s}s "
                          f"then attempting recovery (round {mcp_recovery_attempt + 1}/"
                          f"{MCP_RECOVERY_MAX_RETRIES})...")
                    await asyncio.sleep(wait_s)
                    try:
                        await _reconnect_evaluator_mcp_session()
                        TRACE.log("mcp_transport_recovered",
                                  attempt=mcp_recovery_attempt + 1)
                        print(f"  MCP RECOVERED on attempt {mcp_recovery_attempt + 1}; "
                              f"resuming eval with reset browser state.")
                        mcp_recovery_attempt += 1
                        mcp_just_recovered = True
                        break
                    except Exception as reconn_err:
                        TRACE.log("mcp_reconnect_failed",
                                  attempt=mcp_recovery_attempt + 1,
                                  retry_cap=MCP_RECOVERY_MAX_RETRIES,
                                  error_type=type(reconn_err).__name__,
                                  error=str(reconn_err)[:500])
                        print(f"  MCP RECOVERY FAILED on attempt "
                              f"{mcp_recovery_attempt + 1}: "
                              f"{type(reconn_err).__name__}: {str(reconn_err)[:200]}")
                        mcp_recovery_attempt += 1
                        last_reconn_err = reconn_err
                if mcp_just_recovered:
                    continue  # restart outer eval loop with the recovery preamble
                # Recovery exhausted — log and fall through to incomplete path below.
                TRACE.log("mcp_recovery_exhausted",
                          total_attempts=mcp_recovery_attempt,
                          last_reconnect_error=(str(last_reconn_err)[:500]
                                                if last_reconn_err else None))
                print(f"  MCP recovery exhausted after {mcp_recovery_attempt} "
                      f"attempts; routing to verdict=incomplete.")

            matched = next(
                (p for p in EVAL_INCOMPLETE_EXCEPTION_PATTERNS if p.lower() in err_lc),
                None,
            )
            if matched:
                TRACE.log("evaluator_verdict_incomplete_due_to_infrastructure",
                          matched_pattern=matched,
                          error_type=type(e).__name__,
                          error=err_str[:1000])
                print(f"\n  EVALUATOR CRASH ({type(e).__name__}: {err_str[:200]}) — "
                      f"verdict='incomplete' (infrastructure failure: {matched!r}).")
                notes = (
                    f"Evaluator could not produce a verdict because of an INFRASTRUCTURE "
                    f"failure (matched pattern {matched!r}): {type(e).__name__}: "
                    f"{err_str[:600]}. The builder cannot fix this — it's an MCP / Playwright / "
                    f"browser issue outside the app under test. The harness will terminate the "
                    f"run; the operator must repair the infrastructure (rebuild playwright-mcp, "
                    f"check MCP transport, confirm Firefox is installed at the version the MCP "
                    f"expects) before re-running. The builder's last work is preserved in the "
                    f"workspace."
                )
                _finalize_iteration_summary(state=state, verdict="incomplete")
                return {"eval_verdict": "incomplete", "eval_notes": notes}
            print(f"\n  EVALUATOR CRASH ({type(e).__name__}: {err_str[:200]}) — returning 'continue'.")
            notes = (
                f"Evaluator crashed with {type(e).__name__}: {err_str[:300]}. Treat as 'unable to "
                f"verify'; the next planner should narrow the eval instructions or check whether "
                f"a harness tool needs fixing (full traceback in trace as evaluator_exception)."
            )
            _finalize_iteration_summary(state=state, verdict="continue")
            return {"eval_verdict": "continue", "eval_notes": notes}

        verdict = _extract_verdict(text)
        notes = _extract_notes(text) or text

        # Layer 2a: empty-notes rejection. On web-app tasks, NOTES under EVAL_NOTES_MIN_CHARS
        # is rejected and the eval is re-invoked with a corrective preamble that asks for
        # findings from already-observed data (no more exploration). Cap=1: second empty
        # notes → escalate to verdict=incomplete (genuine eval failure to produce findings).
        # Cheap-tier evaluators (qwen-coder etc.) consistently fail to recover on the retry
        # — observed empirically — so skip the retry for them and go straight to the salvage
        # path. Override with HARNESS_EVAL_RETRY_EMPTY_NOTES_ON_CHEAP=1 if testing a model
        # known to behave differently.
        if is_web_app and _eval_notes_too_short(notes):
            tier_used = (_evaluator_holder.get("last_selection") or {}).get("tier_used") or ""
            cheap_no_retry = (tier_used == "cheap" and not EVAL_RETRY_EMPTY_NOTES_ON_CHEAP)
            effective_cap = 0 if cheap_no_retry else EVAL_EMPTY_NOTES_RETRY_CAP
            if empty_notes_retry < effective_cap:
                TRACE.log("evaluator_empty_notes_rejected",
                          retry_round=empty_notes_retry + 1,
                          retry_cap=effective_cap,
                          notes_chars=len((notes or "").strip()),
                          notes_preview=(notes or "").strip()[:300],
                          rejected_verdict=verdict)
                print(f"\n  REJECTED: empty NOTES on web-app verdict "
                      f"(round {empty_notes_retry + 1}/{effective_cap}). "
                      f"Forcing retry to populate findings.")
                empty_notes_retry += 1
                last_failure = "empty_notes"
                continue
            else:
                if cheap_no_retry and empty_notes_retry == 0:
                    # Log the skip explicitly so it's auditable in the trace why no retry
                    # happened (vs. the more typical "retry cap exhausted" path).
                    TRACE.log("evaluator_empty_notes_retry_skipped",
                              reason="cheap_tier_no_retry",
                              tier_used=tier_used,
                              notes_chars=len((notes or "").strip()),
                              rejected_verdict=verdict)
                    print(f"\n  SKIPPING empty-notes retry on cheap-tier evaluator "
                          f"({tier_used}) — observed not to recover. Going straight to "
                          f"salvage-findings path. Set HARNESS_EVAL_RETRY_EMPTY_NOTES_ON_CHEAP=1 "
                          f"to force a retry anyway.")
                # Cap exceeded: escalate to incomplete with a salvage attempt from the
                # tool history. The eval has demonstrated it cannot produce findings even
                # when explicitly asked; treat as an evaluator failure mode, not a routine
                # continue (which would loop the builder on the same untested work).
                # Honest rounds count: standard path is empty_notes_retry+1 (the rejected
                # round + retries). Cheap-skip path is just 1 (no retry was run).
                rounds_attempted = empty_notes_retry + 1
                findings_data = _extract_eval_findings(_eval_tool_history)
                TRACE.log("evaluator_empty_notes_cap_exceeded",
                          retry_cap=effective_cap,
                          rounds_attempted=rounds_attempted,
                          findings_count=len(findings_data["findings"]),
                          pages_ok=findings_data["pages_ok"],
                          pages_with_errors=findings_data["pages_with_errors"],
                          navigates=findings_data.get("navigates", 0),
                          screenshots_taken=findings_data.get("screenshots_taken", 0),
                          interactions=findings_data.get("interactions", 0))
                if cheap_no_retry and rounds_attempted == 1:
                    print(f"\n  ESCALATED: empty NOTES on cheap-tier evaluator "
                          f"(no retry on cheap by default) — verdict=incomplete.")
                    header_text = (
                        f"Evaluator failed to write actionable NOTES (cheap-tier, "
                        f"no-retry policy)."
                    )
                else:
                    print(f"\n  ESCALATED: empty NOTES persisted across "
                          f"{rounds_attempted} round(s) — verdict=incomplete.")
                    header_text = (
                        f"Evaluator failed to write actionable NOTES across "
                        f"{rounds_attempted} round(s)."
                    )
                # Notes start with the marker string "failed to write actionable NOTES" so
                # route_after_eval can distinguish this from infrastructure-failure incomplete.
                notes = _format_findings_for_notes(findings_data, header=header_text)
                verdict = "incomplete"
                break

        # Layer 2b: insufficient evidence on done verdicts (existing). Non-done verdicts
        # (continue/replan/incomplete) are accepted as-is here — the loop is only protecting
        # against false-done.
        if (is_web_app
                and verdict == "done"
                and evidence_retry < EVAL_INSUFFICIENT_EVIDENCE_RETRY_CAP):
            shortfall = _eval_evidence_shortfall(tool_counter)
            if shortfall:
                TRACE.log("evaluator_verdict_rejected_insufficient_evidence",
                          retry_round=evidence_retry + 1,
                          retry_cap=EVAL_INSUFFICIENT_EVIDENCE_RETRY_CAP,
                          tool_counter=dict(tool_counter),
                          shortfall=shortfall,
                          rejected_verdict=verdict)
                print(f"\n  REJECTED: 'done' verdict without interaction evidence "
                      f"(round {evidence_retry + 1}/{EVAL_INSUFFICIENT_EVIDENCE_RETRY_CAP}).")
                for s in shortfall:
                    print(f"    - {s}")
                evidence_retry += 1
                last_failure = "evidence"
                continue
        break

    print(f"\n  VERDICT: {verdict}")
    print(f"  NOTES: {_truncate_simple(notes, 400)}")
    TRACE.log("verdict", verdict=verdict, notes=notes[:1000],
              tool_counter=dict(tool_counter), is_web_app=is_web_app,
              evidence_retry_rounds=evidence_retry,
              empty_notes_retry_rounds=empty_notes_retry)

    # Test gate: run between verdict and checkpoint. Iterations that regress tests
    # don't get committed — the builder is asked to fix the regression first. Gate
    # only runs on claimed-success verdicts (done | continue) and only when enabled +
    # baseline wasn't already broken. Other verdicts (replan / incomplete / builder-
    # exit) skip the gate because there's no claimed completion to verify against.
    test_gate_info: dict | None = None  # passed into _finalize_iteration_summary for history
    skip_checkpoint = False
    if (TEST_GATE_ENABLED
            and not _test_gate_state["disabled_baseline"]
            and verdict in ("done", "continue")):
        print(f"\n  Running test gate ({TEST_COMMAND!r}, cwd={TEST_CWD})...")
        t0 = time.monotonic()
        gate_status, gate_err, gate_output = _run_test_gate(TEST_CWD)
        dt = round(time.monotonic() - t0, 2)
        _test_gate_state["last_status"] = gate_status
        _test_gate_state["last_duration_seconds"] = dt
        _test_gate_state["last_output_tail"] = gate_output

        if gate_status == "passed":
            TRACE.log("test_gate_passed", iteration=state["iteration"],
                      duration_seconds=dt, command=TEST_COMMAND)
            print(f"  Test gate PASSED in {dt}s.")
            _test_gate_state["failure_streak"] = 0
            test_gate_info = {"status": "passed", "duration_seconds": dt,
                              "streak": 0, "checkpointed": True}
        else:
            # Failed or timeout
            _test_gate_state["failure_streak"] += 1
            streak = _test_gate_state["failure_streak"]
            TRACE.log(
                "test_gate_timeout" if gate_status == "timeout" else "test_gate_failed",
                iteration=state["iteration"], duration_seconds=dt, status=gate_status,
                error=gate_err, output_tail=gate_output[:2000],
                failure_streak=streak, max_streak=TEST_GATE_MAX_STREAK,
            )
            print(f"  Test gate {gate_status.upper()} ({gate_err}); "
                  f"streak {streak}/{TEST_GATE_MAX_STREAK}.")

            if not TEST_GATE_REQUIRED_FOR_CHECKPOINT:
                # Warnings-only mode: log + proceed to checkpoint as usual. The iteration
                # history shows "tests: <status>; checkpointed anyway".
                print(f"  HARNESS_TEST_GATE_REQUIRED=0; checkpointing despite failure.")
                test_gate_info = {"status": gate_status, "duration_seconds": dt,
                                  "streak": streak, "checkpointed": True,
                                  "error": gate_err}
            else:
                # Required mode: skip checkpoint, demote verdict to continue (or escalate
                # to incomplete on circuit-breaker). The builder gets the regression
                # message in eval_notes via the planner on the next iteration.
                skip_checkpoint = True
                failure_notice = (
                    f"\n\n[test gate] The previous iteration appeared to complete the work, "
                    f"but the project's test suite regressed.\n"
                    f"Test command: {TEST_COMMAND}\n"
                    f"Status: {gate_status} ({gate_err})\n"
                    f"Output tail:\n{gate_output}\n\n"
                    f"The work was NOT committed. Fix the test failures before calling "
                    f"verify_completion again. You may need to revisit recent edits or run "
                    f"additional verification."
                )

                if streak >= TEST_GATE_MAX_STREAK:
                    # Circuit breaker — terminate the run. Notes start with a marker
                    # string route_after_eval can recognize for the dedicated diagnostic.
                    TRACE.log("test_gate_circuit_breaker_triggered",
                              iteration=state["iteration"],
                              failure_streak=streak,
                              max_streak=TEST_GATE_MAX_STREAK,
                              last_output_tail=gate_output[:2000],
                              command=TEST_COMMAND)
                    print(f"  TEST GATE CIRCUIT BREAKER: streak "
                          f"{streak}/{TEST_GATE_MAX_STREAK} — terminating run.")
                    notes = (
                        f"[test gate circuit breaker] Run terminated: tests failing for "
                        f"{streak} consecutive iteration(s) (cap={TEST_GATE_MAX_STREAK}).\n\n"
                        f"Test command: {TEST_COMMAND}\n"
                        f"Status: {gate_status} ({gate_err})\n"
                        f"Last test output:\n{gate_output}"
                    )
                    verdict = "incomplete"
                    test_gate_info = {"status": gate_status, "duration_seconds": dt,
                                      "streak": streak, "checkpointed": False,
                                      "circuit_breaker": True, "error": gate_err}
                else:
                    # Below cap: demote done → continue, skip checkpoint, append the
                    # failure notice. Builder rerun will see the notice in the next
                    # planner's BUILDER_INSTRUCTIONS via eval_notes carryover.
                    if verdict == "done":
                        verdict = "continue"
                    notes = notes + failure_notice
                    test_gate_info = {"status": gate_status, "duration_seconds": dt,
                                      "streak": streak, "checkpointed": False,
                                      "error": gate_err}
    elif TEST_GATE_ENABLED and _test_gate_state["disabled_baseline"]:
        # Gate is enabled at config-level but suppressed because the baseline was broken.
        TRACE.log("test_gate_skipped", iteration=state["iteration"],
                  reason="baseline_failing")

    # Per-iteration checkpoint — runs UNLESS test gate failed in required mode.
    if not skip_checkpoint:
        _create_checkpoint(
            iteration=state["iteration"],
            task=state.get("task", ""),
            plan_doc=plan_doc,
            verdict=verdict,
            builder_model=builder_llm.model_name,
        )

    # Cache this verdict for future skip-eval decisions. Only refresh when the verdict is
    # one of {done, continue} — incomplete and replan are not safe fallback verdicts for
    # subsequent iterations to inherit. Note we cache the full notes (capped) so a skipped
    # iteration can show the previous evidence rather than a generic message.
    update: dict = {"eval_verdict": verdict, "eval_notes": notes}
    if verdict in ("done", "continue") and not skip_checkpoint:
        update["last_successful_eval"] = {
            "iteration": state["iteration"],
            "verdict": verdict,
            "notes_preview": notes[:2000],
            "ts": datetime.now(timezone.utc).isoformat(),
        }
    _finalize_iteration_summary(state=state, verdict=verdict, test_gate_info=test_gate_info)
    return update


