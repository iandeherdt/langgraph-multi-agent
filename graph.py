# Per-model cumulative usage. Populated by _record_cost as model calls complete; read by
# _update_run_summary for the cost line + by _emit_run_cost_summary at session end.
_cost_tracker: dict = {
    "by_model": {},   # model_name -> {input, output, calls, cost_usd}
    "total_usd": 0.0,
    "started_at": None,
}

# Per-evaluator-invocation cost tracker. Reset to 0 at the top of each evaluator_node call;
# accumulated by _record_cost when label="eval". Read by _stream_subagent (when label="eval")
# to enforce HARNESS_EVALUATOR_COST_LIMIT_EUR. Stored in EUR so the cap reads naturally.
_current_evaluator_cost_eur: float = 0.0

# Last evaluator cost-limit event. Captured once per evaluator_node call when the cap fires.
# Read by _update_run_summary's Status block to render the "Evaluator cost limit hit at iter N"
# line. Persisted across resume via state.json (last_evaluator_cost_eur) so the operator can
# see partial cost on a budget-terminated run.
_evaluator_cost_limit_event: dict = {
    "iteration": None,
    "cost_eur": 0.0,
    "limit_eur": 0.0,
    "tool_calls_made": 0,
    "last_tool": None,
}


class EvaluatorBudgetExhausted(Exception):
    """Raised inside _stream_subagent when label='eval' and the per-evaluator cost cap is hit.
    Caught by evaluator_node, which synthesizes a verdict=incomplete from the partial
    evidence in _eval_tool_history.
    """
    def __init__(self, *, cost_eur: float, limit_eur: float, last_tool: str | None = None):
        super().__init__(
            f"Evaluator cost limit exceeded: €{cost_eur:.4f} >= €{limit_eur:.4f} "
            f"(last tool: {last_tool!r})"
        )
        self.cost_eur = cost_eur
        self.limit_eur = limit_eur
        self.last_tool = last_tool


def _normalize_model_for_pricing(model: str) -> tuple[str, dict] | tuple[None, dict]:
    """Match `model` (any of the slug forms LangChain returns) against COST_PER_1M_TOKENS.

    Returns (matched_key, rates) on hit; (None, {}) when nothing matches and we should
    record at zero cost. Case-insensitive substring match — pricing keys are intentionally
    short ("claude-sonnet-4-6", "qwen3-coder") so a longer slug matches if it contains
    the key as substring.
    """
    if not model:
        return None, {}
    lower = model.lower()
    for key, rates in COST_PER_1M_TOKENS.items():
        if key.lower() in lower:
            return key, rates
    return None, {}


def _record_cost(model: str, input_tokens: int, output_tokens: int, *, label: str = "") -> None:
    """Update the cumulative cost tracker for one model call. Logs a model_call_cost trace
    event so post-hoc analysis can correlate cost to specific iterations / labels."""
    if not (input_tokens or output_tokens):
        return  # nothing to record; some streams don't surface usage_metadata
    if _cost_tracker["started_at"] is None:
        _cost_tracker["started_at"] = datetime.now(timezone.utc).isoformat()

    matched, rates = _normalize_model_for_pricing(model)
    cost_usd = (input_tokens * rates.get("input", 0.0)
                + output_tokens * rates.get("output", 0.0)) / 1_000_000.0

    by_model = _cost_tracker["by_model"].setdefault(
        model, {"input": 0, "output": 0, "calls": 0, "cost_usd": 0.0,
                "matched_pricing_key": matched})
    by_model["input"] += int(input_tokens or 0)
    by_model["output"] += int(output_tokens or 0)
    by_model["calls"] += 1
    by_model["cost_usd"] += cost_usd
    _cost_tracker["total_usd"] += cost_usd

    # Per-evaluator-invocation accumulator. Resets at the top of each evaluator_node call;
    # the streaming loop reads it to enforce HARNESS_EVALUATOR_COST_LIMIT_EUR.
    if label == "eval":
        global _current_evaluator_cost_eur
        _current_evaluator_cost_eur += cost_usd * COST_USD_TO_EUR

    TRACE.log("model_call_cost", model=model, label=label,
              input_tokens=int(input_tokens or 0),
              output_tokens=int(output_tokens or 0),
              cost_usd=round(cost_usd, 6),
              matched_pricing_key=matched,
              total_usd=round(_cost_tracker["total_usd"], 4))


def _dedupe_repeated_string(s: str) -> str:
    """Return the smallest repeating unit of `s` if `s` is exactly N copies of some prefix,
    else `s` unchanged. Recovers from langchain AIMessageChunk's __add__ concatenating
    `response_metadata.model_name` across chunks (so a 3-chunk stream produces
    "qwen/qwen3.6-27bqwen/qwen3.6-27bqwen/qwen3.6-27b" as the model name in the cost tracker).
    """
    n = len(s)
    if n <= 1:
        return s
    for k in range(1, n // 2 + 1):
        if n % k == 0 and s[:k] * (n // k) == s:
            return s[:k]
    return s


def _extract_usage(msg) -> tuple[int, int, str]:
    """Extract (input_tokens, output_tokens, model_name) from a langchain message.

    AIMessages from LangChain v0.3+ carry .usage_metadata = {'input_tokens', 'output_tokens',
    'total_tokens'}. Older / vendor wrappers sometimes use .response_metadata.token_usage =
    {'prompt_tokens', 'completion_tokens'} (OpenAI shape) or
    .response_metadata.usage = {'input_tokens', 'output_tokens'} (Anthropic shape). We try
    each shape; absent usage returns (0, 0, "").

    The model_name is run through _dedupe_repeated_string because AIMessageChunk's __add__
    operator concatenates string fields in response_metadata across chunks via langchain's
    _merge_dicts. A 2-chunk stream produces "<name><name>" as the model_name; recovering
    the intended slug avoids the corrupted name leaking into _cost_tracker as a dict key.
    """
    def _name(value: str) -> str:
        return _dedupe_repeated_string((value or "").strip())

    if msg is None:
        return 0, 0, ""
    # Modern, vendor-agnostic
    um = getattr(msg, "usage_metadata", None)
    if um:
        rm = getattr(msg, "response_metadata", {}) or {}
        return int(um.get("input_tokens", 0) or 0), int(um.get("output_tokens", 0) or 0), _name(rm.get("model_name", ""))
    rm = getattr(msg, "response_metadata", {}) or {}
    # OpenAI / OpenRouter shape
    tu = rm.get("token_usage")
    if tu:
        return int(tu.get("prompt_tokens", 0) or 0), int(tu.get("completion_tokens", 0) or 0), _name(rm.get("model_name", ""))
    # Anthropic native shape
    u = rm.get("usage")
    if u:
        return int(u.get("input_tokens", 0) or 0), int(u.get("output_tokens", 0) or 0), _name(rm.get("model", ""))
    return 0, 0, ""


# Per-iteration history. Each entry = {iter, status, summary, verdict, advisor_missing,
# touched_count, cost_usd_delta, commit_hash}. Cleared at process start; populated by
# _record_iteration_history at iteration-end. The RUN_SUMMARY.md "Iteration history"
# section is rendered from this list.
_iteration_history: list[dict] = []
# Per-iteration summary cache: verify_completion captures task_summary into here so the
# iteration-history line can reuse it. Module-level (not in State) because verify_completion
# is a @tool function.
_iteration_summary_holder: dict = {"task_summary": "", "advisor_missing": []}


def _reset_iteration_summary_holder() -> None:
    _iteration_summary_holder["task_summary"] = ""
    _iteration_summary_holder["advisor_missing"] = []


def _summary_line_for_early_exit(*, exit_signal: str, exit_payload: dict | None,
                                 plan_doc: dict, file_count: int) -> str:
    """Produce a tailored one-line summary for an early-exit iteration where
    verify_completion never ran. The default builder-summary first line ("Builder
    exited: model_unreachable (after 0 steps)") is too generic for the iteration-history
    list — this version surfaces the most useful detail per signal type and includes
    file/plan progress so the iteration's actual scope is visible at a glance.
    """
    payload = exit_payload or {}
    tasks = (plan_doc or {}).get("tasks") or []
    done = sum(1 for t in tasks if t.get("status") == "done")
    total = len(tasks)

    # Per-signal detail extraction. Each exit tool stores different keys in payload.
    detail = ""
    if exit_signal == "model_unreachable":
        et = payload.get("error_type", "")
        em = (payload.get("error") or "")[:100]
        detail = f"{et}: {em}".strip(": ") if (et or em) else ""
    elif exit_signal == "give_up":
        detail = (payload.get("reason") or "")[:120]
    elif exit_signal == "help":
        detail = (payload.get("reason") or "")[:120]
    elif exit_signal == "replan":
        detail = (payload.get("rationale") or "")[:120]

    head = exit_signal if not detail else f"{exit_signal} — {detail}"
    progress = f"{file_count} file(s) written, plan {done}/{total}"
    return f"crashed/exited: {head}; {progress}"


def _record_iteration_history(*, iteration: int, status: str, verdict: str,
                              touched_count: int, commit_hash: str | None,
                              advisor_missing: list | None = None,
                              builder_summary: str = "",
                              exit_signal: str | None = None,
                              exit_payload: dict | None = None,
                              plan_doc: dict | None = None,
                              test_gate_info: dict | None = None) -> None:
    """Append one entry to _iteration_history with a one-line summary line.

    Summary precedence: for early-exit status, generate from exit_signal + payload +
    counts (the builder's verbose default summary line is too generic). Otherwise:
    verify_completion's task_summary (most informative) → builder summary's first
    non-empty line → "exploration" (last-resort fallback).
    """
    summary = ""
    if status == "early-exit" and exit_signal:
        summary = _summary_line_for_early_exit(
            exit_signal=exit_signal,
            exit_payload=exit_payload,
            plan_doc=plan_doc or {},
            file_count=touched_count,
        )
    if not summary:
        summary = _iteration_summary_holder.get("task_summary") or ""
    if not summary and builder_summary:
        first = builder_summary.strip().splitlines()[0] if builder_summary.strip() else ""
        summary = first
    if not summary:
        summary = "exploration (no completion attempted)"
    if len(summary) > RUN_SUMMARY_TASK_SUMMARY_CHARS:
        summary = summary[: RUN_SUMMARY_TASK_SUMMARY_CHARS - 1] + "…"
    advisor_missing = (advisor_missing
                       or _iteration_summary_holder.get("advisor_missing")
                       or [])
    cost_so_far_usd = _cost_tracker["total_usd"]
    _iteration_history.append({
        "iter": iteration,
        "status": status,        # "ok" | "rejected" | "incomplete" | "early-exit"
        "verdict": verdict,
        "summary": summary,
        "advisor_missing": list(advisor_missing)[:5],
        "touched_count": touched_count,
        "cost_usd_at_end": round(cost_so_far_usd, 4),
        "commit_hash": commit_hash or "",
        # Stored for _gather_open_concerns to surface failure context when the most
        # recent iteration was early-exit / incomplete and the advisor never produced
        # a missing list to surface.
        "exit_signal": exit_signal or "",
        "exit_payload_detail": _shorten_exit_payload(exit_signal, exit_payload),
        # Test-gate annotation for the iteration-history line. None when gate didn't run.
        "test_gate": dict(test_gate_info) if test_gate_info else None,
    })


def _shorten_exit_payload(exit_signal: str | None, exit_payload: dict | None) -> str:
    """Pull the most-relevant single-line detail from an exit payload for surfacing in
    Open concerns. Returns '' when nothing useful is available."""
    if not exit_payload:
        return ""
    payload = exit_payload
    if exit_signal == "model_unreachable":
        et = payload.get("error_type", "")
        em = (payload.get("error") or "")[:200]
        return f"{et}: {em}".strip(": ") if (et or em) else ""
    if exit_signal == "give_up":
        return (payload.get("reason") or "")[:200]
    if exit_signal == "help":
        return (payload.get("reason") or "")[:200]
    if exit_signal == "replan":
        return (payload.get("rationale") or "")[:200]
    return ""


_VERDICT_TO_STATUS = {
    "done": "ok",
    "continue": "ok",
    "replan": "rejected",
    "incomplete": "incomplete",
}


def _format_cost_line() -> str:
    """One-line cost summary in the CCY the user picked (€ default, $ via env)."""
    use_eur = os.environ.get("HARNESS_COST_CCY", "EUR").upper() == "EUR"
    sym = "€" if use_eur else "$"
    factor = COST_USD_TO_EUR if use_eur else 1.0
    total = _cost_tracker["total_usd"] * factor
    parts = []
    for model, e in _cost_tracker["by_model"].items():
        amt = e["cost_usd"] * factor
        parts.append(f"{_compact_model_name(model)}: {sym}{amt:.4f}")
    breakdown = " · ".join(parts) if parts else "(no usage recorded)"
    return f"{sym}{total:.4f} so far  ·  {breakdown}"


def _compact_model_name(model: str) -> str:
    """Trim provider prefix for display ('anthropic/claude-sonnet-4-6' -> 'claude-sonnet-4-6')."""
    if "/" in model:
        return model.rsplit("/", 1)[-1]
    return model


def _gather_open_concerns(state) -> list[str]:
    """Collect actionable concerns for the summary's 'Open concerns' section.

    Sources, in order:
      - Most recent iteration with status != ok → advisor_missing items (when present)
        AND exit_signal+payload (when builder ended early without the advisor running)
        AND verdict-based hint when status is 'incomplete' (eval-side failures)
      - Pending architecture proposals on the plan
    """
    concerns: list[str] = []
    for entry in reversed(_iteration_history):
        if entry["status"] == "ok":
            continue
        # Most recent non-ok iteration. Surface advisor missing items if the advisor
        # actually ran — those are usually the most actionable signals.
        for m in entry.get("advisor_missing") or []:
            concerns.append(f"(iter {entry['iter']}) {m}")
        # When the iteration was an early-exit (builder crashed / give_up / help / replan),
        # the advisor never ran — surface the exit signal + payload detail instead so the
        # human/operator can see why the run stopped.
        if entry["status"] == "early-exit" and entry.get("exit_signal"):
            sig = entry["exit_signal"]
            detail = entry.get("exit_payload_detail") or ""
            label_map = {
                "model_unreachable": "model unreachable",
                "give_up": "builder called give_up",
                "help": "builder called request_user_help",
                "replan": "builder called revise_plan",
            }
            label = label_map.get(sig, sig)
            line = f"(iter {entry['iter']}) builder ended early — {label}"
            if detail:
                line += f": {detail}"
            concerns.append(line)
        # When the iteration was 'incomplete' (eval-side: MCP infra failure or empty-notes
        # cap exceeded), the eval_notes contain the diagnostic — pull a one-liner.
        elif entry["status"] == "incomplete" and not entry.get("advisor_missing"):
            concerns.append(
                f"(iter {entry['iter']}) eval returned 'incomplete'; check trace for "
                f"evaluator_exception / evaluator_empty_notes_cap_exceeded"
            )
        break  # only the most recent non-ok iteration

    # Pending architecture proposals (orthogonal to verdict status).
    plan = state.get("plan") if state else None
    if isinstance(plan, dict):
        for prop in plan.get("pending_proposals") or []:
            concerns.append(f"(architecture) {prop.get('section','?')}: {prop.get('change','?')}")
    return concerns[:10]


def _next_planned_actions(state, max_n: int = 5) -> list[str]:
    """Pick up to `max_n` outstanding plan tasks (status in {todo, doing}) for the
    'Next planned actions' section."""
    plan = (state or {}).get("plan") or {}
    out: list[str] = []
    for t in plan.get("tasks") or []:
        if t.get("status") in ("todo", "doing"):
            text = (t.get("text") or "").strip()
            marker = "→" if t.get("status") == "doing" else " "
            out.append(f"{marker} #{t.get('id','?')}: {text[:120]}")
            if len(out) >= max_n:
                break
    return out


def _update_run_summary(state) -> None:
    """Write/overwrite RUN_SUMMARY.md with the current run state.

    Idempotent and fail-soft: any I/O error logs a trace event and does not propagate.
    Called from end of evaluator_node and from builder early-exit returns.
    """
    try:
        HARNESS_DIR.mkdir(parents=True, exist_ok=True)
        task = (state or {}).get("task", "(no task)")
        plan = (state or {}).get("plan") or {}
        tasks = plan.get("tasks") or []
        done_n = sum(1 for t in tasks if t.get("status") == "done")
        total_n = len(tasks)
        iterations_n = (state or {}).get("iteration", 0)
        branch = _git_checkpoint_state.get("branch") or "(no branch)"
        last_commit = _git_checkpoint_state.get("last_commit") or {}
        cost_line = _format_cost_line()

        history_lines = []
        if not _iteration_history:
            history_lines.append("- (none yet — first iteration not finished)")
        else:
            for entry in _iteration_history:
                marker = {"ok": "✓", "rejected": "✗", "incomplete": "⚠", "early-exit": "↩"}.get(
                    entry["status"], "•")
                hash_str = f" [{entry['commit_hash']}]" if entry["commit_hash"] else ""
                # Strip "builder_exit:" prefix from verdict — the ↩ marker already conveys
                # "early exit" and the early-exit summary line embeds the signal name, so
                # leaving the prefix in produces redundant noise like
                # "(builder_exit:model_unreachable, 6 files)" on a line that already says
                # "crashed/exited: model_unreachable …".
                v = entry["verdict"]
                if v.startswith("builder_exit:"):
                    v = v[len("builder_exit:"):]
                file_word = "file" if entry["touched_count"] == 1 else "files"
                # Test-gate annotation when the gate ran for this iteration.
                tg = entry.get("test_gate") or None
                tg_part = ""
                if tg:
                    if tg["status"] == "passed":
                        tg_part = f"; tests: passed in {tg['duration_seconds']}s"
                    elif tg.get("circuit_breaker"):
                        tg_part = (f"; tests: {tg['status']}; circuit breaker fired "
                                   f"(streak {tg['streak']}/{TEST_GATE_MAX_STREAK})")
                    elif tg.get("checkpointed"):
                        tg_part = (f"; tests: {tg['status']}; checkpointed anyway "
                                   f"(streak {tg['streak']}/{TEST_GATE_MAX_STREAK})")
                    else:
                        tg_part = (f"; tests: {tg['status']}; not committed "
                                   f"(streak {tg['streak']}/{TEST_GATE_MAX_STREAK})")
                history_lines.append(
                    f"- Iter {entry['iter']}: {entry['summary']} "
                    f"({v}, {entry['touched_count']} {file_word}{tg_part}){hash_str} {marker}"
                )

        concerns = _gather_open_concerns(state)
        concerns_block = "\n".join(f"- {c}" for c in concerns) if concerns else "- (none)"

        next_actions = _next_planned_actions(state)
        next_block = "\n".join(f"- {a}" for a in next_actions) if next_actions else "- (none — plan complete or empty)"

        started = _cost_tracker.get("started_at") or datetime.now(timezone.utc).isoformat()
        now = datetime.now(timezone.utc).isoformat()

        last_ckpt_line = "(none)"
        if last_commit:
            last_ckpt_line = f"iter {last_commit.get('iter','?')}, commit {last_commit.get('hash','?')}"

        truncated_task = task.strip().splitlines()[0] if task else "(no task)"
        if len(truncated_task) > 200:
            truncated_task = truncated_task[:197] + "..."

        # Resume hint — only shown when checkpointing produced a real branch (which means
        # state.json is being persisted by _save_run_state). The exact command makes resume
        # one-paste-from-the-summary.
        run_id = _current_run_id()
        if run_id and RESUME_ENABLED:
            resume_block = (
                f"## Resume\n"
                f"This run can be resumed (after a crash, Ctrl-C, or budget exhaustion) with:\n\n"
                f"    ./run.sh --resume {run_id}\n\n"
            )
        else:
            resume_block = ""

        # Test gate section — only when the gate is configured. Includes the disabled-
        # baseline notice when applicable.
        if TEST_GATE_ENABLED:
            tg = _test_gate_state
            required_str = "Yes" if TEST_GATE_REQUIRED_FOR_CHECKPOINT else "No (warnings only)"
            last_status = tg.get("last_status") or "(not run yet)"
            last_dur = tg.get("last_duration_seconds")
            last_dur_s = f"{last_dur}s" if last_dur is not None else "—"
            test_gate_block = (
                f"## Test gate\n"
                f"- Command: `{TEST_COMMAND}`\n"
                f"- CWD: `{TEST_CWD}`\n"
                f"- Timeout: {TEST_TIMEOUT_SECONDS}s\n"
                f"- Required for checkpoint: {required_str}\n"
                f"- Failure streak: {tg.get('failure_streak', 0)}/{TEST_GATE_MAX_STREAK}\n"
                f"- Last result: {last_status}\n"
                f"- Last duration: {last_dur_s}\n"
            )
            if tg.get("disabled_baseline"):
                test_gate_block += (
                    f"\n**Disabled for this run:** tests were already failing at start.\n\n"
                    f"Last test output (tail):\n```\n{tg.get('last_output_tail', '')[:1500]}\n```\n"
                )
            test_gate_block += "\n"
        else:
            test_gate_block = ""

        # Termination section — fires when the test-gate circuit breaker tripped OR the
        # evaluator cost cap (HARNESS_EVALUATOR_COST_LIMIT_EUR) fired on the latest
        # iteration. Surfaces the bypass commands the spec requires.
        last_entry = _iteration_history[-1] if _iteration_history else None
        termination_block = ""
        ev_cost = _evaluator_cost_limit_event
        if (last_entry and ev_cost.get("iteration") == last_entry.get("iter")):
            # Evaluator cost-cap termination. Render its own block separately from the
            # run-total cost so the operator can see the eval-only cost.
            run_total_eur = _cost_tracker["total_usd"] * COST_USD_TO_EUR
            run_id_for_resume = _current_run_id() or "<run-id>"
            termination_block += (
                f"## Termination\n"
                f"Run terminated by evaluator cost cap.\n\n"
                f"- Reason: evaluator cost reached €{ev_cost['cost_eur']:.4f} "
                f"(cap=€{ev_cost['limit_eur']:.4f}) at iter {ev_cost['iteration']}\n"
                f"- Tool calls made before cap: {ev_cost.get('tool_calls_made', 0)}; "
                f"last tool: `{ev_cost.get('last_tool')!r}`\n"
                f"- Eval cost (this iteration): €{ev_cost['cost_eur']:.4f}\n"
                f"- Run-total cost: €{run_total_eur:.4f}\n\n"
                f"To raise the per-evaluator cap on the resumed run:\n\n"
                f"    HARNESS_EVALUATOR_COST_LIMIT_EUR=2.00 ./run.sh --resume {run_id_for_resume}\n\n"
                f"To disable the cap entirely (use sparingly — protects against runaway "
                f"verification loops):\n\n"
                f"    HARNESS_EVALUATOR_COST_LIMIT_ENABLED=0 ./run.sh --resume {run_id_for_resume}\n\n"
            )
        if last_entry and (last_entry.get("test_gate") or {}).get("circuit_breaker"):
            tgc = last_entry["test_gate"]
            last_h = last_commit.get("hash") or "(none)"
            run_id_for_resume = run_id or "<run-id>"
            termination_block += (
                f"## Termination\n"
                f"Run terminated by test gate.\n\n"
                f"- Reason: tests failing for {tgc['streak']} consecutive iteration(s) "
                f"(cap={TEST_GATE_MAX_STREAK})\n"
                f"- Test command: `{TEST_COMMAND}`\n"
                f"- Last successful checkpoint: iter {last_commit.get('iter','?')}, "
                f"commit {last_h}\n\n"
                f"To bypass tests for the resumed run:\n\n"
                f"    HARNESS_TEST_GATE_REQUIRED=0 ./run.sh --resume {run_id_for_resume}\n\n"
                f"To increase tolerance:\n\n"
                f"    HARNESS_TEST_GATE_MAX_STREAK=<higher> ./run.sh --resume {run_id_for_resume}\n\n"
            )

        md = (
            f"# Run summary: {truncated_task}\n\n"
            f"Started: {started}\n"
            f"Last updated: {now}\n"
            f"Run branch: {branch}\n\n"
            + resume_block
            + termination_block
            + f"## Status\n"
            f"- Plan tasks: {done_n} done / {total_n} total\n"
            f"- Iterations: {iterations_n}\n"
            f"- Last successful checkpoint: {last_ckpt_line}\n"
            f"- Estimated cost so far: {cost_line}\n"
            + _format_evaluator_model_line()
            + "\n"
            + test_gate_block
            + f"## Iteration history\n"
            + "\n".join(history_lines) + "\n\n"
            f"## Open concerns\n"
            f"{concerns_block}\n\n"
            f"## Next planned actions\n"
            f"{next_block}\n"
        )

        # Atomic write: write to .tmp then rename, so a crash mid-write doesn't leave a
        # partial file that the next builder iteration might read into context.
        tmp = RUN_SUMMARY_PATH.with_suffix(RUN_SUMMARY_PATH.suffix + ".tmp")
        tmp.write_text(md)
        tmp.replace(RUN_SUMMARY_PATH)
        TRACE.log("run_summary_updated", path=str(RUN_SUMMARY_PATH), bytes=len(md),
                  iterations=iterations_n, cost_usd=round(_cost_tracker["total_usd"], 4))
    except Exception as e:
        TRACE.log("run_summary_failed", error=str(e)[:300])


def _finalize_iteration_summary(*, state, verdict: str, builder_summary: str = "",
                                exit_signal: str | None = None,
                                exit_payload: dict | None = None,
                                test_gate_info: dict | None = None) -> None:
    """Record the iteration in history + rewrite RUN_SUMMARY.md + persist state.json.
    Idempotent. Called from every return path in evaluator_node and from builder_node
    early-exit returns. The iteration history's status is derived from the verdict via
    _VERDICT_TO_STATUS; for builder-side early exits (e.g., 'builder_exit:give_up') the
    status is 'early-exit'.

    test_gate_info: optional dict {status, duration_seconds, streak, checkpointed,
    error?, circuit_breaker?} for the iteration history's test-gate annotation. None
    when the gate didn't run for this iteration (early-exit, replan, gate disabled, etc.).
    """
    if verdict.startswith("builder_exit:"):
        status = "early-exit"
    else:
        status = _VERDICT_TO_STATUS.get(verdict, "rejected")
    last_commit = (_git_checkpoint_state.get("last_commit") or {}).get("hash")
    _record_iteration_history(
        iteration=(state or {}).get("iteration", 0),
        status=status,
        verdict=verdict,
        touched_count=len((state or {}).get("iteration_files_touched") or []),
        commit_hash=last_commit,
        builder_summary=builder_summary,
        exit_signal=exit_signal,
        exit_payload=exit_payload,
        plan_doc=(state or {}).get("plan") or {},
        test_gate_info=test_gate_info,
    )
    _update_run_summary(state)
    # Persist a per-run state.json so a Ctrl-C / OOM / container-restart can be resumed
    # via `./run.sh --resume <run_id>`. Originated task (the user's first input on the
    # original run start) is captured by main() into _run_started_holder; subsequent
    # iterations + resumes preserve it across the full life of the run.
    _save_run_state(state, original_task=_run_started_holder.get("original_task", ""))
    _reset_iteration_summary_holder()


# Captures the run's originating task. Populated once in main() at task start; used by
# _save_run_state so the persisted original_task survives across iterations + resumes.
_run_started_holder: dict = {"original_task": ""}


# Test-gate runtime state. Populated by the baseline check and the per-iteration gate run.
# Persisted in state.json (resume schema v2) and restored on resume. Cleared at process
# start; resume seeds it from the loaded state.
_test_gate_state: dict = {
    "disabled_baseline": False,    # True if baseline failed → gates suppressed for this run
    "failure_streak": 0,           # consecutive failures since last pass
    "last_status": None,           # "passed" | "failed" | "timeout" | "skipped" | None
    "last_duration_seconds": None, # float; for the Test gate section
    "last_output_tail": "",        # for surfacing in builder context on next iteration
}


def _run_test_gate(workspace_path: str = TEST_CWD) -> tuple[str, str | None, str]:
    """Run the configured test command under a timeout. Returns (status, error, output).

    status one of:
      - "skipped" — TEST_GATE_ENABLED is False (no command configured)
      - "passed" — exit 0
      - "failed" — non-zero exit
      - "timeout" — exceeded TEST_TIMEOUT_SECONDS

    output is a TEST_OUTPUT_TAIL_LINES tail of stdout+stderr combined; empty string when
    the run produced nothing. Smart truncation: if total output ≤ tail, returns all of it.
    """
    if not TEST_GATE_ENABLED:
        return "skipped", "no test command configured", ""
    try:
        proc = subprocess.run(
            TEST_COMMAND,
            shell=True,
            cwd=workspace_path,
            capture_output=True,
            text=True,
            timeout=TEST_TIMEOUT_SECONDS,
        )
    except subprocess.TimeoutExpired as e:
        partial = ((e.stdout or "") + (e.stderr or ""))
        return ("timeout", f"test command exceeded {TEST_TIMEOUT_SECONDS}s",
                _tail_lines(partial, TEST_OUTPUT_TAIL_LINES))
    except Exception as e:
        return "failed", f"{type(e).__name__}: {str(e)[:200]}", ""

    output = (proc.stdout or "") + (proc.stderr or "")
    tail = _tail_lines(output, TEST_OUTPUT_TAIL_LINES)
    if proc.returncode == 0:
        return "passed", None, tail
    return "failed", f"exit code {proc.returncode}", tail


def _tail_lines(s: str, n: int) -> str:
    """Return the last n lines of s. Cheap; no truncation marker because the gate output
    is intended for human + model consumption, not byte-exact diff."""
    if not s:
        return ""
    lines = s.splitlines()
    if len(lines) <= n:
        return s
    return "\n".join(lines[-n:])


def _read_run_summary_for_builder() -> str:
    """Read RUN_SUMMARY.md if it exists, return the contents to inject into the builder's
    system prompt as 'PREVIOUS RUN STATE'. Returns empty string when the file doesn't
    exist (e.g., iteration 1 — no prior state to display)."""
    try:
        if RUN_SUMMARY_PATH.exists():
            return RUN_SUMMARY_PATH.read_text()
    except Exception:
        pass
    return ""


